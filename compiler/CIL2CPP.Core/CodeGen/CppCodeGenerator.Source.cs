    /// <summary>
    /// Generate stub file: fallback implementations for methods that should be eliminated
    /// by better tree-shaking. Instance methods on CoreRuntimeTypes are NOT included —
    /// the runtime library (core_methods.cpp) provides their extern "C" definitions.
    /// </summary>
    private GeneratedFile GenerateStubFile()
    {
        var sb = new StringBuilder();
        EmitSourceFileHeader(sb, "Stubs: unreachable method placeholders (tree-shaking gaps)");

        // Forward declarations for NativeLibrary QCall implementations
        EmitNativeLibraryQCallDecls(sb);

        // Partition: CoreRuntimeTypes methods vs everything else.
        // Both partitions get stub bodies — CoreRuntimeTypes methods can't compile from IL
        // (struct layout mismatch) but callers in other BCL code still reference them.
        var (glue, stubs) = PartitionMissingMethods(_emittedMethodSignatures);

        // Diagnostic: report stub counts to stderr for auditing
        if (glue.Count > 0 || stubs.Count > 0)
        {
            Console.Error.WriteLine($"[CIL2CPP] Linker stub summary for {_module.Name}:");
            if (glue.Count > 0)
                Console.Error.WriteLine($"  Runtime glue (CoreRuntime/ClrInternal): {glue.Count} methods");
            if (stubs.Count > 0)
            {
                Console.Error.WriteLine($"  Unreachable stubs (tree-shaking gaps): {stubs.Count} methods");
                var byType = stubs.GroupBy(m => m.DeclaringType?.ILFullName ?? "<unknown>")
                    .OrderByDescending(g => g.Count()).Take(20);
                foreach (var g in byType)
                    Console.Error.WriteLine($"    [{g.Count()}x] {g.Key}");
            }
        }

        if (glue.Count > 0)
            GenerateMissingMethodImpls(sb, glue, "Runtime Type Method Stubs (CoreRuntimeTypes — struct layout prevents IL compilation)");
        if (stubs.Count > 0)
            GenerateMissingMethodImpls(sb, stubs, "Unreachable Method Stubs (tree-shaking gaps — should be eliminated)");

        GenerateSafeHandleMarshallerImpls(sb);

        return new GeneratedFile
        {
            FileName = $"{_module.Name}_stubs.cpp",
            Content = sb.ToString()
        };
    }

    /// <summary>
    /// Generate C++ implementations for SafeHandleMarshaller&lt;T&gt;.ManagedToUnmanagedIn
    /// methods (FromManaged, ToUnmanaged, Free) for opaque marshaller stubs.
    /// These are trivial wrappers around SafeHandle ICalls (DangerousAddRef/GetHandle/Release).
    /// </summary>
    private void GenerateSafeHandleMarshallerImpls(StringBuilder sb)
    {
        if (_opaqueSafeHandleMarshallerStubs.Count == 0) return;

        sb.AppendLine();
        sb.AppendLine("// ===== SafeHandleMarshaller ManagedToUnmanagedIn Implementations =====");
        foreach (var typeName in _opaqueSafeHandleMarshallerStubs)
        {
            // Skip types that already have compiled methods from IL
            bool hasCompiledVersion = false;
            foreach (var sig in _emittedMethodSignatures)
            {
                if (sig.Contains($"{typeName}_FromManaged("))
                { hasCompiledVersion = true; break; }
            }
            if (hasCompiledVersion) continue;

            sb.AppendLine();
            sb.AppendLine($"// SafeHandleMarshaller ManagedToUnmanagedIn: {typeName}");
            // FromManaged: store handle + DangerousAddRef
            sb.AppendLine($"void {typeName}_FromManaged({typeName}* __this, void* handle) {{");
            sb.AppendLine($"    __this->f__handle = handle;");
            sb.AppendLine($"    cil2cpp::icall::SafeHandle_DangerousAddRef(handle, &__this->f__addRefd);");
            sb.AppendLine($"}}");
            // ToUnmanaged: DangerousGetHandle → IntPtr
            sb.AppendLine($"intptr_t {typeName}_ToUnmanaged({typeName}* __this) {{");
            sb.AppendLine($"    return cil2cpp::icall::SafeHandle_DangerousGetHandle(__this->f__handle);");
            sb.AppendLine($"}}");
            // Free: DangerousRelease if addRefd
            sb.AppendLine($"void {typeName}_Free({typeName}* __this) {{");
            sb.AppendLine($"    if (__this->f__addRefd) {{");
            sb.AppendLine($"        cil2cpp::icall::SafeHandle_DangerousRelease(__this->f__handle);");
            sb.AppendLine($"    }}");
            sb.AppendLine($"}}");
        }
    }

    /// <summary>
    /// Partition types into adaptively-sized buckets using greedy bin-packing.
    /// Partition count is deterministic (based on code size only, not CPU cores)
    /// so the same input always produces the same output on any machine.
    /// Returns non-empty partitions only.
    /// </summary>
    private static List<List<IRType>> PartitionTypes(List<IRType> types)
    {
        // Filter to types with emittable methods, sorted largest-first
        var typesWithMethods = types
            .Select(t => (type: t, size: EstimateMethodSize(t)))
            .Where(x => x.size > 0)
            .OrderByDescending(x => x.size)
            .ToList();

        if (typesWithMethods.Count == 0)
            return new List<List<IRType>> { new() };

        int totalSize = typesWithMethods.Sum(x => x.size);

        // Adaptive partition count: min of two constraints
        // 1. Size-based: each partition should have enough code to amortize header re-parsing
        int fromSize = Math.Max(1, totalSize / MinInstructionsPerPartition);
        // 2. Type-based: can't split finer than per-type
        int fromTypes = typesWithMethods.Count;

        // Note: we do NOT use Environment.ProcessorCount here — generated output must be
        // deterministic (same input → same files on any machine). The build system (cmake --parallel)
        // handles core scheduling.
        int partitionCount = Math.Max(1, Math.Min(fromSize, fromTypes));

        var partitions = new (List<IRType> types, int totalSize)[partitionCount];
        for (int i = 0; i < partitionCount; i++)
            partitions[i] = (new List<IRType>(), 0);

        // Greedy: assign each type to the partition with the smallest total size
        foreach (var (type, size) in typesWithMethods)
        {
            int minIdx = 0;
            for (int i = 1; i < partitionCount; i++)
            {
                if (partitions[i].totalSize < partitions[minIdx].totalSize)
                    minIdx = i;
            }
            partitions[minIdx].types.Add(type);
            partitions[minIdx].totalSize += size;
        }

        return partitions.Select(p => p.types).Where(l => l.Count > 0).ToList();
    }

    /// <summary>
    /// Estimate the total method body size for a type (instruction count across all emittable methods).
    /// </summary>
    private static int EstimateMethodSize(IRType type)
    {
        if (type.IsDelegate) return 0;
        int total = 0;
        foreach (var method in type.Methods)
        {
            if (method.IsAbstract || method.IsInternalCall || method.IsPInvoke) continue;
            if (method.BasicBlocks.Count == 0) continue;
            if (method.HasICallMapping) continue;
            total += method.BasicBlocks.Sum(b => b.Instructions.Count);
        }
        return total;
    }

    /// <summary>
    /// Partition declared-but-not-defined methods into two categories:
    /// 1. Runtime glue: methods on CoreRuntimeTypes/SkipAllMethodsTypes/ClrInternalTypeNames
    ///    (provided by C++ runtime, stubs are linker glue only)
    /// 2. Unreachable stubs: methods that should be eliminated by better tree-shaking
    /// </summary>
    private (List<IRMethod> glue, List<IRMethod> stubs) PartitionMissingMethods(HashSet<string> emittedSignatures)
    {
        // Build lookup: method CppName → list of IRMethods (multiple overloads possible)
        var methodLookup = new Dictionary<string, List<IRMethod>>();
        foreach (var type in _module.Types)
        {
            foreach (var method in type.Methods)
            {
                if (!methodLookup.TryGetValue(method.CppName, out var list))
                {
                    list = new List<IRMethod>();
                    methodLookup[method.CppName] = list;
                }
                list.Add(method);
            }
        }

        var glue = new List<IRMethod>();
        var stubs = new List<IRMethod>();

        foreach (var funcName in _declaredFunctionNames)
        {
            if (!methodLookup.TryGetValue(funcName, out var methods)) continue;
            foreach (var irMethod in methods)
            {
                var sig = irMethod.GetCppSignature();
                if (emittedSignatures.Contains(sig)) continue;

                // Classify: runtime-provided types → glue; everything else → stub
                var typeName = irMethod.DeclaringType?.ILFullName ?? "";
                if (IRBuilder.CoreRuntimeTypes.Contains(typeName))
                {
                    // CoreRuntimeType methods are declared as extern "C" in the header
                    // (by GenerateMissingMethodStubs). Generating stub bodies would conflict
                    // with the extern "C" definitions in core_methods.cpp (LNK2005).
                    // Skip ALL CoreRuntimeType stubs — missing impls surface as linker errors
                    // and should be added to core_methods.cpp or registered as ICalls.
                    _emittedMethodSignatures.Add(sig);
                    continue;
                }
                else if (IRBuilder.SkipAllMethodsTypes.Contains(typeName)
                    || IRBuilder.ClrInternalTypeNames.Contains(typeName))
                {
                    glue.Add(irMethod);
                }
                else
                {
                    stubs.Add(irMethod);
                }
            }
        }

        return (glue, stubs);
    }

    /// <summary>
    /// Emit method implementations for a list of missing methods with a given section header.
    /// Shared logic for both runtime glue and unreachable stubs.
    /// </summary>
    private void GenerateMissingMethodImpls(StringBuilder sb, List<IRMethod> methods, string sectionHeader)
    {
        if (methods.Count == 0) return;

        sb.AppendLine();
        sb.AppendLine($"// ===== {sectionHeader} =====");

        // Collect all by-value types from signatures that are forward-declared only.
        var opaqueTypes = new HashSet<string>();
        foreach (var method in methods)
        {
            var typesToCheck = method.Parameters.Select(p => p.CppTypeName).ToList();
            if (!string.IsNullOrEmpty(method.ReturnTypeCpp) && method.ReturnTypeCpp != "void")
                typesToCheck.Add(method.ReturnTypeCpp);
            foreach (var typeName in typesToCheck)
            {
                if (typeName.EndsWith("*")) continue;
                var baseType = typeName.Trim();
                if (string.IsNullOrEmpty(baseType)) continue;
                if (IsCppPrimitiveType(baseType)) continue;
                if (baseType.StartsWith("cil2cpp::")) continue;
                if (_emittedStructDefs.Contains(baseType)) continue;
                if (!IsValidCppIdentifier(baseType)) continue;
                opaqueTypes.Add(baseType);
            }
        }
        if (opaqueTypes.Count > 0)
        {
            sb.AppendLine("// Opaque type definitions for forward-declared-only types used by value");
            foreach (var typeName in opaqueTypes.OrderBy(n => n))
                sb.AppendLine($"struct {typeName} {{ }}; // opaque — forward-declared only");
            sb.AppendLine();
        }

        // Track emitted function overloads to avoid return-type-only overloads.
        var emittedOverloads = new HashSet<string>();
        string GetOverloadKey(IRMethod m) =>
            $"{m.CppName}({string.Join(", ", m.Parameters.Select(p => p.CppTypeName))})";
        foreach (var type in _module.Types)
        {
            foreach (var m in type.Methods)
            {
                if (_emittedMethodSignatures.Contains(m.GetCppSignature()))
                    emittedOverloads.Add(GetOverloadKey(m));
            }
        }

        foreach (var method in methods.OrderBy(m => m.CppName))
        {
            var sig = method.GetCppSignature();
            if (!_emittedMethodSignatures.Add(sig)) continue;
            if (!emittedOverloads.Add(GetOverloadKey(method))) continue;

            // Skip methods with unresolved generic params
            bool hasProblematicType = false;
            var stubTypesToCheck = method.Parameters.Select(p => p.CppTypeName).ToList();
            if (!string.IsNullOrEmpty(method.ReturnTypeCpp) && method.ReturnTypeCpp != "void")
                stubTypesToCheck.Add(method.ReturnTypeCpp);
            foreach (var typeName in stubTypesToCheck)
            {
                var baseType = typeName.TrimEnd('*').Trim();
                if (string.IsNullOrEmpty(baseType)) continue;
                if (!baseType.Contains('_') && !baseType.StartsWith("cil2cpp::") &&
                    !IsCppPrimitiveType(baseType) && !_knownTypeNames.Contains(baseType))
                {
                    hasProblematicType = true;
                    break;
                }
            }
            if (hasProblematicType) continue;

            // Check if this is a NativeLibrary QCall with a known implementation
            var nativeLibBody = GetNativeLibraryQCallBody(method.CppName);
            if (nativeLibBody != null)
            {
                sb.AppendLine($"{sig} {{");
                sb.AppendLine(nativeLibBody);
                sb.AppendLine("}");
                continue;
            }

            sb.AppendLine($"{sig} {{");
            sb.AppendLine($"#ifndef NDEBUG");
            sb.AppendLine($"    cil2cpp::stub_called(\"{method.CppName}\");");
            sb.AppendLine($"#endif");
            if (method.ReturnTypeCpp == "void" || string.IsNullOrEmpty(method.ReturnTypeCpp))
                sb.AppendLine("    // stub: method unreachable (tree-shaking gap)");
            else if (method.ReturnTypeCpp.EndsWith("*"))
                sb.AppendLine("    return nullptr; // stub: method unreachable (tree-shaking gap)");
            else if (method.ReturnTypeCpp == "bool")
                sb.AppendLine("    return false; // stub: method unreachable (tree-shaking gap)");
            else
                sb.AppendLine("    return {}; // stub: method unreachable (tree-shaking gap)");
            sb.AppendLine("}");
        }
    }

    /// <summary>
    /// Emits file-scope forward declarations for NativeLibrary QCall implementations.
    /// </summary>
    private static void EmitNativeLibraryQCallDecls(StringBuilder sb)
    {
        sb.AppendLine();
        sb.AppendLine("// Forward declarations for NativeLibrary QCall implementations");
        sb.AppendLine("#ifdef _WIN32");
        sb.AppendLine("extern \"C\" __declspec(dllimport) void* __stdcall LoadLibraryW(const wchar_t*);");
        sb.AppendLine("extern \"C\" __declspec(dllimport) int __stdcall FreeLibrary(void*);");
        sb.AppendLine("extern \"C\" __declspec(dllimport) void* __stdcall GetProcAddress(void*, const char*);");
        sb.AppendLine("extern \"C\" __declspec(dllimport) int __stdcall WideCharToMultiByte(unsigned, unsigned long, const wchar_t*, int, char*, int, const char*, int*);");
        sb.AppendLine("#else");
        sb.AppendLine("#include <dlfcn.h>");
        sb.AppendLine("#endif");
    }

    /// <summary>
    /// Provides platform-specific implementations for NativeLibrary QCall stubs.
    /// These BCL methods use [DllImport("QCall")] which has no real DLL — they need
    /// runtime implementations for LoadLibrary/GetProcAddress/FreeLibrary.
    /// </summary>
    private static string? GetNativeLibraryQCallBody(string cppName)
    {
        // Match the compiler-generated __PInvoke wrapper names for NativeLibrary QCalls.
        // The numeric suffixes may vary across BCL versions, so match by prefix.
        if (cppName.Contains("NativeLibrary") && cppName.Contains("PInvoke"))
        {
            if (cppName.Contains("_GetSymbol_"))
                return """
                        // QCall: NativeLibrary.GetSymbol → GetProcAddress/dlsym
                    #ifdef _WIN32
                        auto handle = reinterpret_cast<void*>(__handle_native);
                        char narrow[256];
                        int len = WideCharToMultiByte(65001, 0, reinterpret_cast<const wchar_t*>(__symbolName_native), -1, narrow, 256, nullptr, nullptr);
                        if (len <= 0) return 0;
                        return reinterpret_cast<intptr_t>(GetProcAddress(handle, narrow));
                    #else
                        (void)__throwOnError_native;
                        auto handle = reinterpret_cast<void*>(__handle_native);
                        char narrow[256];
                        size_t i = 0;
                        for (; __symbolName_native[i] && i < 255; ++i) narrow[i] = static_cast<char>(__symbolName_native[i]);
                        narrow[i] = 0;
                        return reinterpret_cast<intptr_t>(dlsym(handle, narrow));
                    #endif
                    """;

            if (cppName.Contains("_LoadFromPath_"))
                return """
                        // QCall: NativeLibrary.LoadFromPath → LoadLibraryW/dlopen
                    #ifdef _WIN32
                        return reinterpret_cast<intptr_t>(LoadLibraryW(reinterpret_cast<const wchar_t*>(__libraryName_native)));
                    #else
                        (void)__throwOnError_native;
                        char narrow[512];
                        size_t i = 0;
                        for (; __libraryName_native[i] && i < 511; ++i) narrow[i] = static_cast<char>(__libraryName_native[i]);
                        narrow[i] = 0;
                        return reinterpret_cast<intptr_t>(dlopen(narrow, RTLD_LAZY));
                    #endif
                    """;

            if (cppName.Contains("_LoadByName_"))
                return """
                        // QCall: NativeLibrary.LoadByName → LoadLibraryW/dlopen
                    #ifdef _WIN32
                        return reinterpret_cast<intptr_t>(LoadLibraryW(reinterpret_cast<const wchar_t*>(__libraryName_native)));
                    #else
                        (void)__throwOnError_native;
                        char narrow[512];
                        size_t i = 0;
                        for (; __libraryName_native[i] && i < 511; ++i) narrow[i] = static_cast<char>(__libraryName_native[i]);
                        narrow[i] = 0;
                        return reinterpret_cast<intptr_t>(dlopen(narrow, RTLD_LAZY));
                    #endif
                    """;

            if (cppName.Contains("FreeLib"))
                return """
                        // QCall: NativeLibrary.FreeLib → FreeLibrary/dlclose
                    #ifdef _WIN32
                        FreeLibrary(reinterpret_cast<void*>(handle));
                    #else
                        dlclose(reinterpret_cast<void*>(handle));
                    #endif
                    """;
        }

        return null;
    }

    private void GenerateTypeInfo(StringBuilder sb, IRType type)
    {
        // For delegate types skipped by EmitReflectionMetadata, emit type-level custom attrs
        // (Enum types now go through EmitReflectionMetadata for full field metadata)
        if (type.IsDelegate && type.CustomAttributes.Count > 0)
        {
            EmitAttributeInfoArray(sb, $"{type.CppName}_custom_attrs", type.CustomAttributes);
        }

        var baseName = type.BaseType != null ? $"&{type.BaseType.CppName}_TypeInfo" : "nullptr";

        // Flags (needed by both full and minimal)
        var flagParts = new List<string>();
        if (type.IsValueType) flagParts.Add("cil2cpp::TypeFlags::ValueType");
        if (type.IsInterface) flagParts.Add("cil2cpp::TypeFlags::Interface");
        if (type.IsAbstract) flagParts.Add("cil2cpp::TypeFlags::Abstract");
        if (type.IsSealed) flagParts.Add("cil2cpp::TypeFlags::Sealed");
        if (type.IsEnum) flagParts.Add("cil2cpp::TypeFlags::Enum");
        if (type.IsPublic) flagParts.Add("cil2cpp::TypeFlags::Public");
        if (type.IsNestedPublic) flagParts.Add("cil2cpp::TypeFlags::NestedPublic");
        if (type.IsNotPublic) flagParts.Add("cil2cpp::TypeFlags::NotPublic");
        if (type.IsNestedAssembly) flagParts.Add("cil2cpp::TypeFlags::NestedAssembly");
        if (type.IsByRefLike) flagParts.Add("cil2cpp::TypeFlags::IsByRefLike");
        // Nullable<T> detection: open generic "System.Nullable`1" or closed "System.Nullable`1<...>"
        if (type.ILFullName.StartsWith("System.Nullable`1"))
            flagParts.Add("cil2cpp::TypeFlags::Nullable");
        var flagsStr = flagParts.Count > 0 ? string.Join(" | ", flagParts) : "cil2cpp::TypeFlags::None";

        // Array flag and rank: detect array types from ILFullName suffix
        int arrayRank = 0;
        if (type.ILFullName.EndsWith("[]"))
        {
            flagParts.Add("cil2cpp::TypeFlags::Array");
            arrayRank = 1;
            // Derive element type CppName from array type ILFullName
            var elementIL = type.ILFullName[..^2]; // strip "[]"
            type.ArrayElementTypeCppName = CppNameMapper.MangleTypeName(elementIL);
        }
        // Multi-dimensional array detection: names end with "[,]", "[,,]", etc.
        else if (type.ILFullName.EndsWith("]") && type.ILFullName.Contains("[,"))
        {
            flagParts.Add("cil2cpp::TypeFlags::Array");
            flagParts.Add("cil2cpp::TypeFlags::MultiDimensionalArray");
            // Rank = number of commas + 1 in the bracket suffix
            var bracketStart = type.ILFullName.LastIndexOf('[');
            arrayRank = type.ILFullName[bracketStart..].Count(c => c == ',') + 1;
        }

        // CorElementType (needed by both full and minimal)
        var corElementType = GetCorElementType(type);

        // Generic variance data (needed by both full and minimal for assignability)
        var typeInfoLookup = BuildTypeInfoExprLookup();
        var hasGenericArgs = type.IsGenericInstance && type.GenericArguments.Count > 0
                             && type.GenericParameterVariances.Count > 0
                             && !type.GenericParameterVariances.All(v => v == 0) // invariant types don't need variance data
                             && type.GenericArguments.All(arg => typeInfoLookup.ContainsKey(arg));

        // Interfaces (pointer array, needed by both tiers for assignability)
        var interfacesExpr = (!type.IsInterface && type.Interfaces.Count > 0) ? $"{type.CppName}_interfaces" : "nullptr";
        var interfaceCount = type.IsInterface ? 0 : type.Interfaces.Count;

        // TypeInfo tiering: non-constructed types whose _TypeInfo is not referenced in code
        // get minimal TypeInfo (identity + hierarchy + flags only). Omitted fields are zero-initialized.
        var constructedTypes = _module.ConstructedTypes;
        var isMinimalTypeInfo = constructedTypes.Count > 0
            && !constructedTypes.Contains(type.ILFullName)
            && !_referencedTypeInfoNames.Contains(type.CppName);

        if (isMinimalTypeInfo)
        {
            // Tier 3 (stub): no interfaces, no inherited interface vtables, no generic args, not enum → single-line TypeInfo
            bool isStubTypeInfo = interfaceCount == 0 && type.InterfaceImpls.Count == 0
                && !hasGenericArgs && !(type.IsEnum && type.EnumUnderlyingType != null);
            if (isStubTypeInfo)
            {
                sb.AppendLine($"cil2cpp::TypeInfo {type.CppName}_TypeInfo = {{ .name = \"{type.Name}\", .namespace_name = \"{type.Namespace}\", .full_name = \"{type.ILFullName}\", .base_type = {baseName}, .flags = {flagsStr}, .cor_element_type = 0x{corElementType:X2} }};");
                return;
            }

            // Tier 2 (minimal): has interfaces/generics/enum — multi-line but reduced
            sb.AppendLine($"cil2cpp::TypeInfo {type.CppName}_TypeInfo = {{");
            sb.AppendLine($"    .name = \"{type.Name}\",");
            sb.AppendLine($"    .namespace_name = \"{type.Namespace}\",");
            sb.AppendLine($"    .full_name = \"{type.ILFullName}\",");
            sb.AppendLine($"    .base_type = {baseName},");
            if (interfaceCount > 0)
            {
                sb.AppendLine($"    .interfaces = {interfacesExpr},");
                sb.AppendLine($"    .interface_count = {interfaceCount},");
            }
            sb.AppendLine($"    .flags = {flagsStr},");
            // Interface vtables: even minimal-tier types need interface_vtables when they have
            // interfaces, because derived constructed types walk the base chain for dispatch.
            if (!type.IsInterface && type.InterfaceImpls.Count > 0)
            {
                var minIfaceVtablesExpr = $"{type.CppName}_interface_vtables";
                sb.AppendLine($"    .interface_vtables = {minIfaceVtablesExpr},");
                sb.AppendLine($"    .interface_vtable_count = {type.InterfaceImpls.Count},");
            }
            sb.AppendLine($"    .cor_element_type = 0x{corElementType:X2},");
            if (arrayRank > 0)
                sb.AppendLine($"    .array_rank = {arrayRank},");
            if (type.IsEnum && type.EnumUnderlyingType != null)
            {
                var underlyingCpp = CppNameMapper.MangleTypeName(type.EnumUnderlyingType);
                sb.AppendLine($"    .underlying_type = &{underlyingCpp}_TypeInfo,");
            }
            if (hasGenericArgs)
            {
                // Tier 2 types still need full generic variance data for runtime
                // assignability checks (e.g., ICovariant<string> vs ICovariant<object>).
                sb.AppendLine($"    .generic_arguments = {type.CppName}_generic_args,");
                sb.AppendLine($"    .generic_variances = {type.CppName}_generic_variances,");
                sb.AppendLine($"    .generic_argument_count = {type.GenericArguments.Count},");
                var minGenDefName = type.GenericDefinitionCppName != null
                    ? $"\"{type.GenericDefinitionCppName}\"" : "nullptr";
                sb.AppendLine($"    .generic_definition_name = {minGenDefName},");
            }
            // Element type info (for array types)
            if (type.ArrayElementTypeCppName != null)
                sb.AppendLine($"    .element_type_info = &{type.ArrayElementTypeCppName}_TypeInfo,");
            // Metadata token
            if (type.MetadataToken != 0)
                sb.AppendLine($"    .metadata_token = 0x{type.MetadataToken:X8},");
            sb.AppendLine("};");
            return;
        }

        // Full TypeInfo: all fields populated

        // VTable (delegates have no vtable)
        var vtableExpr = (!type.IsInterface && !type.IsDelegate && type.VTable.Count > 0) ? $"&{type.CppName}_VTable" : "nullptr";

        // Interface vtables (also skipped for interfaces)
        var ifaceVtablesExpr = (!type.IsInterface && type.InterfaceImpls.Count > 0) ? $"{type.CppName}_interface_vtables" : "nullptr";
        var ifaceVtableCount = type.IsInterface ? 0 : type.InterfaceImpls.Count;

        // Finalizer
        var finalizerExpr = (type.Finalizer != null && !type.IsRuntimeProvided) ? $"{type.CppName}_finalizer_wrapper" : "nullptr";

        // Instance size (interfaces have no struct)
        var instanceSize = type.IsInterface ? "0" : $"sizeof({type.CppName})";

        // Compute reflection metadata expressions
        var allFields = type.Fields.Concat(type.StaticFields).ToList();
        var reflectableMethods = type.Methods.Where(m => !CppNameMapper.IsCompilerGeneratedType(m.Name)).ToList();
        var reflectableProperties = type.Properties;
        var reflectionTargets = _module.ReflectionTargetTypes;
        // Field/method metadata: gated by reflection targets + enum types
        var skipFieldMethodReflection = type.IsDelegate || type.IsRuntimeProvided
            || CppNameMapper.IsRuntimeExceptionType(type.ILFullName)
            || (allFields.Count == 0 && reflectableMethods.Count == 0)
            || (!type.IsEnum && reflectionTargets.Count > 0 && !reflectionTargets.Contains(type.ILFullName));
        // Property metadata: always emitted for non-runtime types with properties
        var skipPropertyReflection = type.IsDelegate || type.IsRuntimeProvided
            || CppNameMapper.IsRuntimeExceptionType(type.ILFullName)
            || reflectableProperties.Count == 0;
        var skipReflection = skipFieldMethodReflection && skipPropertyReflection;
        var fieldsExpr = (!skipFieldMethodReflection && allFields.Count > 0) ? $"{type.CppName}_fields" : "nullptr";
        var methodsExpr = (!skipFieldMethodReflection && reflectableMethods.Count > 0) ? $"{type.CppName}_methods" : "nullptr";
        var propsExpr = (!skipPropertyReflection && reflectableProperties.Count > 0) ? $"{type.CppName}_properties" : "nullptr";
        if (skipFieldMethodReflection) { allFields = new(); reflectableMethods = new(); }
        if (skipPropertyReflection) { reflectableProperties = new(); }
        var typeAttrsExpr = (!skipReflection && type.CustomAttributes.Count > 0)
            ? $"{type.CppName}_custom_attrs" : "nullptr";
        var typeAttrCount = skipReflection ? 0 : type.CustomAttributes.Count;
        var underlyingTypeExpr = "nullptr";
        if (type.IsEnum && type.EnumUnderlyingType != null)
        {
            var underlyingCpp = CppNameMapper.MangleTypeName(type.EnumUnderlyingType);
            underlyingTypeExpr = $"&{underlyingCpp}_TypeInfo";
        }
        // Enum name/value metadata for Enum.ToString
        var enumNamesExpr = "nullptr";
        var enumValuesExpr = "nullptr";
        var enumCount = 0;
        if (type.IsEnum)
        {
            var enumFields = type.StaticFields
                .Where(f => f.ConstantValue != null)
                .OrderBy(f => EnumConstantToInt64(f.ConstantValue!))
                .ToList();
            if (enumFields.Count > 0)
            {
                enumCount = enumFields.Count;
                var namesArr = string.Join(", ", enumFields.Select(f => $"\"{f.Name}\""));
                var valuesArr = string.Join(", ", enumFields.Select(f => FormatEnumInt64(EnumConstantToInt64(f.ConstantValue!))));
                sb.AppendLine($"static const char* {type.CppName}_enum_names[] = {{ {namesArr} }};");
                sb.AppendLine($"static int64_t {type.CppName}_enum_values[] = {{ {valuesArr} }};");
                enumNamesExpr = $"{type.CppName}_enum_names";
                enumValuesExpr = $"{type.CppName}_enum_values";
            }
        }
        var genArgsExpr = hasGenericArgs ? $"{type.CppName}_generic_args" : "nullptr";
        var genVarExpr = hasGenericArgs ? $"{type.CppName}_generic_variances" : "nullptr";
        var genCount = hasGenericArgs ? type.GenericArguments.Count : 0;
        var genDefName = type.GenericDefinitionCppName != null
            ? $"\"{type.GenericDefinitionCppName}\"" : "nullptr";

        // Emit full TypeInfo with compaction: skip null/0 fields (C++20 zero-inits omitted fields)
        sb.AppendLine($"cil2cpp::TypeInfo {type.CppName}_TypeInfo = {{");
        sb.AppendLine($"    .name = \"{type.Name}\",");
        sb.AppendLine($"    .namespace_name = \"{type.Namespace}\",");
        sb.AppendLine($"    .full_name = \"{type.ILFullName}\",");
        if (baseName != "nullptr")
            sb.AppendLine($"    .base_type = {baseName},");
        if (interfaceCount > 0)
        {
            sb.AppendLine($"    .interfaces = {interfacesExpr},");
            sb.AppendLine($"    .interface_count = {interfaceCount},");
        }
        if (!type.IsInterface)
            sb.AppendLine($"    .instance_size = {instanceSize},");
        // Value types need element_size for array element access (array_get_element_ptr)
        if (type.IsValueType && !type.IsInterface)
            sb.AppendLine($"    .element_size = {instanceSize},");
        sb.AppendLine($"    .flags = {flagsStr},");
        if (vtableExpr != "nullptr")
            sb.AppendLine($"    .vtable = {vtableExpr},");
        if (fieldsExpr != "nullptr")
        {
            sb.AppendLine($"    .fields = {fieldsExpr},");
            sb.AppendLine($"    .field_count = {allFields.Count},");
        }
        if (methodsExpr != "nullptr")
        {
            sb.AppendLine($"    .methods = {methodsExpr},");
            sb.AppendLine($"    .method_count = {reflectableMethods.Count},");
        }
        if (propsExpr != "nullptr")
        {
            sb.AppendLine($"    .properties = {propsExpr},");
            sb.AppendLine($"    .property_count = {reflectableProperties.Count},");
        }
        // .default_ctor always nullptr — omit
        if (finalizerExpr != "nullptr")
            sb.AppendLine($"    .finalizer = {finalizerExpr},");
        if (ifaceVtableCount > 0)
        {
            sb.AppendLine($"    .interface_vtables = {ifaceVtablesExpr},");
            sb.AppendLine($"    .interface_vtable_count = {ifaceVtableCount},");
        }
        if (typeAttrCount > 0)
        {
            sb.AppendLine($"    .custom_attributes = {typeAttrsExpr},");
            sb.AppendLine($"    .custom_attribute_count = {typeAttrCount},");
        }
        sb.AppendLine($"    .cor_element_type = 0x{corElementType:X2},");
        if (arrayRank > 0)
            sb.AppendLine($"    .array_rank = {arrayRank},");
        if (underlyingTypeExpr != "nullptr")
            sb.AppendLine($"    .underlying_type = {underlyingTypeExpr},");
        if (enumCount > 0)
        {
            sb.AppendLine($"    .enum_names = {enumNamesExpr},");
            sb.AppendLine($"    .enum_values = {enumValuesExpr},");
            sb.AppendLine($"    .enum_count = {enumCount},");
        }
        if (hasGenericArgs)
        {
            sb.AppendLine($"    .generic_arguments = {genArgsExpr},");
            sb.AppendLine($"    .generic_variances = {genVarExpr},");
            sb.AppendLine($"    .generic_argument_count = {genCount},");
            sb.AppendLine($"    .generic_definition_name = {genDefName},");
        }
        // Element type info (for array types: T[] → T's TypeInfo)
        if (type.ArrayElementTypeCppName != null)
            sb.AppendLine($"    .element_type_info = &{type.ArrayElementTypeCppName}_TypeInfo,");
        // Metadata token (ECMA-335)
        if (type.MetadataToken != 0)
            sb.AppendLine($"    .metadata_token = 0x{type.MetadataToken:X8},");
        sb.AppendLine("};");
    }

    private void GenerateMethodImpl(StringBuilder sb, IRMethod method,
        List<(string Category, string FunctionName, string InMethod)>? localDeadCode = null)
    {
        sb.AppendLine($"// {method.DeclaringType?.ILFullName}::{method.Name}");
        sb.AppendLine($"{method.GetCppSignature()} {{");

        // MSVC /O2 can clobber register variables across setjmp/longjmp (CIL2CPP_TRY).
        // Declare pointer-type locals as volatile in methods that contain exception handlers.
        bool hasSetjmp = method.BasicBlocks.SelectMany(b => b.Instructions).Any(i => i is IRTryBegin);

        // Declare local variables
        foreach (var local in method.Locals)
        {
            var defaultVal = CppNameMapper.GetDefaultValue(local.CppTypeName);
            // Safety: non-pointer, non-primitive types are value types (structs) in generated code.
            // GetDefaultValue may return "nullptr" for unregistered value types (e.g., opaque BCL stubs).
            // Structs can't be initialized from nullptr — use {} (value initialization) instead.
            if (defaultVal == "nullptr" && !local.CppTypeName.EndsWith("*")
                && !local.CppTypeName.StartsWith("cil2cpp::"))
                defaultVal = "{}";
            // Pointer locals in setjmp functions must be volatile to prevent register clobbering.
            // volatile goes after * so the pointer itself (not the pointee) is volatile.
            if (hasSetjmp && local.CppTypeName.EndsWith("*"))
                sb.AppendLine($"    {local.CppTypeName} volatile {local.CppName} = {defaultVal};");
            else
                sb.AppendLine($"    {local.CppTypeName} {local.CppName} = {defaultVal};");
        }

        // Collect temp variables that need auto declarations
        var declaredTemps = new HashSet<string>();
        string? lastLineDirective = null;

        // Check if method has any labels (branch targets)
        var allInstructions = method.BasicBlocks.SelectMany(b => b.Instructions).ToList();
        bool hasLabels = allInstructions.Any(i => i is IRLabel);

        // Declare __filter_result at method scope if any filter handlers exist.
        // Must be at method scope because CIL2CPP_FILTER_BEGIN opens an else-scope
        // and the variable must be visible across all filters and handler bodies.
        if (allInstructions.Any(i => i is IRFilterBegin))
            sb.AppendLine("    int32_t __filter_result = 0;");

        // Pre-declare temp variables used across label scopes
        // (variables declared inside one { } label scope can't be used in another)
        // Also track cross-scope pointer vars for explicit cast wrapping below.
        Dictionary<string, string>? crossScopePtrVars = null;
        // Determine temp var types for cross-scope declarations and void* arithmetic fix
        var tempVarTypes = DetermineTempVarTypes(allInstructions, method);
        if (hasLabels)
        {
            var crossScopeVars = FindCrossScopeVariables(allInstructions);
            crossScopePtrVars = new Dictionary<string, string>();
            foreach (var varName in crossScopeVars)
            {
                var typeName = tempVarTypes.GetValueOrDefault(varName);
                if (typeName == null)
                {
                    // Type unknown: default to Object* (most cross-scope temps are pointer types)
                    var vol = hasSetjmp ? " volatile" : "";
                    sb.AppendLine($"    cil2cpp::Object*{vol} {varName} = nullptr;");
                    crossScopePtrVars[varName] = "cil2cpp::Object*";
                }
                else
                {
                    var initVal = typeName.EndsWith("*") ? "nullptr" : "{}";
                    var vol = hasSetjmp && typeName.EndsWith("*") ? " volatile" : "";
                    sb.AppendLine($"    {typeName}{vol} {varName} = {initVal};");
                    if (typeName.EndsWith("*"))
                        crossScopePtrVars[varName] = typeName;
                    // Track intptr_t/uintptr_t vars: in IL, native int and pointers are
                    // freely interchangeable. Cross-scope merges may assign pointer values
                    // (uint8_t*, void*, etc.) to intptr_t vars without explicit conv.i/conv.u.
                    else if (typeName is "intptr_t" or "uintptr_t")
                        crossScopePtrVars[varName] = typeName;
                }
                declaredTemps.Add(varName);
            }
        }
        bool inLabelScope = false;
        bool gotoScopeOpen = false;

        // Emit basic blocks
        foreach (var block in method.BasicBlocks)
        {
            foreach (var instr in block.Instructions)
            {
                // When entering a label, close goto scope and label scope
                if (hasLabels && instr is IRLabel)
                {
                    if (gotoScopeOpen)
                    {
                        sb.AppendLine("    }");
                        gotoScopeOpen = false;
                    }
                    if (inLabelScope)
                        sb.AppendLine("    }");
                    // Emit the label at function scope
                    sb.AppendLine($"    {instr.ToCpp()}");
                    sb.AppendLine("    {");
                    inLabelScope = true;
                    continue;
                }

                // Exception handling instructions must be at function scope
                // (CIL2CPP_TRY/CATCH/FINALLY/END_TRY macros expand to { if / } else if / } })
                // IRFilterBegin with IsFirst=false doesn't need scope closing (no } else { emitted)
                // IRFilterHandlerEnd emits a skip label inside the else block
                if (instr is IRTryBegin or IRFinallyBegin or IRTryEnd
                    || (instr is IRFilterBegin fb && fb.IsFirst)
                    || (instr is IRCatchBegin cb && !cb.AfterFilter))
                {
                    if (gotoScopeOpen)
                    {
                        sb.AppendLine("    }");
                        gotoScopeOpen = false;
                    }
                    if (inLabelScope)
                    {
                        sb.AppendLine("    }");
                        inLabelScope = false;
                    }
                }
                // After-filter catches and non-first filters: close label scope but not macro scope
                if ((instr is IRFilterBegin fb2 && !fb2.IsFirst)
                    || (instr is IRCatchBegin cb2 && cb2.AfterFilter)
                    || instr is IRFilterHandlerEnd)
                {
                    if (gotoScopeOpen)
                    {
                        sb.AppendLine("    }");
                        gotoScopeOpen = false;
                    }
                    if (inLabelScope)
                    {
                        sb.AppendLine("    }");
                        inLabelScope = false;
                    }
                }

                // Emit IL offset comment in Debug mode
                if (_config.EmitILOffsetComments && instr.DebugInfo != null && instr.DebugInfo.ILOffset >= 0
                    && instr is not IRComment && instr is not IRLabel)
                {
                    sb.AppendLine($"    /* IL_{instr.DebugInfo.ILOffset:X4} */");
                }

                // Emit #line directive in Debug mode (when source info is available)
                if (_config.EmitLineDirectives && instr.DebugInfo != null
                    && instr.DebugInfo.Line > 0
                    && !string.IsNullOrEmpty(instr.DebugInfo.FilePath)
                    && instr is not IRComment && instr is not IRLabel)
                {
                    // Normalize path separators for C++ (use forward slashes)
                    var filePath = instr.DebugInfo.FilePath.Replace("\\", "/");
                    var directive = $"#line {instr.DebugInfo.Line} \"{filePath}\"";

                    // Only emit if different from the last directive
                    if (directive != lastLineDirective)
                    {
                        sb.AppendLine($"    {directive}");
                        lastLineDirective = directive;
                    }
                }

                var code = instr.ToCpp();

                // Fix void* pointer arithmetic: C++ doesn't allow arithmetic on void*.
                // IL treats native int and void* interchangeably for add/sub (byte-level).
                // Cast void* operands to uint8_t* so the arithmetic compiles.
                if (instr is IRBinaryOp binOp2 && binOp2.Op is "+" or "-")
                {
                    var leftType = tempVarTypes.GetValueOrDefault(binOp2.Left);
                    var rightType = tempVarTypes.GetValueOrDefault(binOp2.Right);
                    if (leftType == "void*" || rightType == "void*")
                    {
                        var fixedLeft = leftType == "void*" ? $"(uint8_t*){binOp2.Left}" : binOp2.Left;
                        var fixedRight = rightType == "void*" ? $"(uint8_t*){binOp2.Right}" : binOp2.Right;
                        code = $"{binOp2.ResultVar} = {fixedLeft} {binOp2.Op} {fixedRight};";
                    }
                }

                // Replace calls to undeclared known-dead-code functions with default values.
                // These are always in guarded dead branches (SIMD: IsSupported=false,
                // EventSource: IsEnabled()=false, ALC: no runtime assembly loading).
                if (instr is IRCall deadCall && _undeclaredFunctionNames.Contains(deadCall.FunctionName))
                {
                    var category = ClassifyDeadCode(deadCall.FunctionName);
                    if (category != DeadCodeCategory.None)
                    {
                        var label = category switch
                        {
                            DeadCodeCategory.Simd => "SIMD",
                            DeadCodeCategory.EventSource => "EventSource",
                            DeadCodeCategory.AotIncompatible => "AOT-incompatible",
                            _ => "dead"
                        };
                        (localDeadCode ?? _deadCodeReplacements).Add((label, deadCall.FunctionName, method.CppName));
                        if (deadCall.ResultVar != null)
                        {
                            var type = deadCall.ResultTypeCpp ?? "int";
                            var defaultVal = type.EndsWith("*") ? "nullptr" : "{}";
                            code = $"{type} {deadCall.ResultVar} = {defaultVal}; // {label} dead-code";
                            declaredTemps.Add(deadCall.ResultVar);
                        }
                        else
                        {
                            code = $"(void)0; // {label} dead-code";
                        }
                    }
                }

                // Strip embedded 'auto' from IRRawCpp when the variable was already
                // pre-declared by cross-scope analysis. Without this, the pre-declared
                // typed variable (e.g. "cil2cpp::Array* __t0 = nullptr;") conflicts with
                // the embedded "auto __t0 = ..." (MSVC C2040: indirection levels differ).
                if (instr is IRRawCpp rawInstr)
                {
                    if (code.StartsWith("auto __t"))
                    {
                        var spaceIdx = code.IndexOf(' ');
                        if (spaceIdx >= 0)
                        {
                            var afterAuto = code[(spaceIdx + 1)..];
                            var eqIdx = afterAuto.IndexOf(' ');
                            var varName = eqIdx >= 0 ? afterAuto[..eqIdx] : afterAuto;
                            if (declaredTemps.Contains(varName))
                                code = afterAuto; // strip "auto " prefix
                        }
                    }
                    // Also handle struct type declarations: "TypeName __tN = {0}; ..."
                    // Strip type prefix when variable is already pre-declared (avoids C2086).
                    else if (rawInstr.ResultVar != null && declaredTemps.Contains(rawInstr.ResultVar)
                             && rawInstr.ResultTypeCpp != null && !rawInstr.ResultTypeCpp.EndsWith("*"))
                    {
                        // Replace "TypeName __tN = {0}; rest" with "std::memset(&__tN, 0, sizeof(TypeName)); rest"
                        var declPattern = $"{rawInstr.ResultTypeCpp} {rawInstr.ResultVar} = {{0}};";
                        if (code.Contains(declPattern))
                        {
                            code = code.Replace(declPattern,
                                $"std::memset(&{rawInstr.ResultVar}, 0, sizeof({rawInstr.ResultTypeCpp}));");
                        }
                    }
                }

                // IRDeclareLocal for value type newobj: "TypeName __tN = {0};"
                // When __tN is already pre-declared (cross-scope), skip the declaration
                // to avoid MSVC C2086 redefinition error. The pre-declaration already
                // initializes the variable with the correct type and default value.
                if (instr is IRDeclareLocal decl && decl.VarName.StartsWith("__t")
                    && declaredTemps.Contains(decl.VarName))
                {
                    code = $"std::memset(&{decl.VarName}, 0, sizeof({decl.TypeName}));";
                }

                // IRDelegateInvoke emits "Type __tN;\n    if (...)" — strip the
                // declaration line when __tN is already pre-declared (cross-scope).
                if (instr is IRDelegateInvoke di && di.ResultVar != null
                    && declaredTemps.Contains(di.ResultVar))
                {
                    var nlIdx = code.IndexOf('\n');
                    if (nlIdx >= 0)
                        code = code[(nlIdx + 1)..].TrimStart();
                }

                // For instructions that assign to temp vars, add 'auto' on first use
                code = AddAutoDeclarations(code, declaredTemps);

                // Wrap plain assignments to cross-scope pointer variables with explicit
                // (void*) casts. Different IL branches may assign different pointer types
                // to the same pre-declared variable; without casts, MSVC rejects the
                // assignment because generated flat structs have no C++ inheritance.
                if (crossScopePtrVars?.Count > 0)
                    code = WrapCrossScopePointerAssignment(code, crossScopePtrVars);

                var indent = (inLabelScope || gotoScopeOpen) ? "        " : "    ";
                sb.AppendLine($"{indent}{code}");

                // After a conditional/unconditional branch in the outer scope,
                // open a scope to prevent goto from crossing auto declarations (C++ C2362)
                if (hasLabels && !inLabelScope && !gotoScopeOpen
                    && (instr is IRConditionalBranch || instr is IRBranch || instr is IRSwitch))
                {
                    sb.AppendLine("    {");
                    gotoScopeOpen = true;
                }
            }
        }

        // Close last goto scope
        if (gotoScopeOpen)
            sb.AppendLine("    }");
        // Close last label scope
        if (inLabelScope)
            sb.AppendLine("    }");

        // In C++, there is no "#line default" like in C#.
        // We simply stop emitting #line directives; the compiler continues
        // using the last #line mapping until it encounters a new one.

        // If method doesn't end with return, add one to satisfy the compiler.
        // This happens when the last instruction is IRTryEnd (try/finally that
        // loops via goto) — the path is unreachable but MSVC warns (C4715).
        var lastInstr = method.BasicBlocks
            .SelectMany(b => b.Instructions)
            .LastOrDefault();
        if (lastInstr is not IRReturn)
        {
            if (method.ReturnTypeCpp == "void")
                sb.AppendLine("    return;");
            else
                sb.AppendLine($"    return {{}};");
        }

        sb.AppendLine("}");
        sb.AppendLine();
    }

    /// <summary>
    /// Find temp variables that are referenced across multiple label scopes.
    /// These must be pre-declared at function scope to avoid C++ scoping issues.
    /// </summary>
    private static HashSet<string> FindCrossScopeVariables(List<IRInstruction> instructions)
    {
        var result = new HashSet<string>();
        // Track which scope each variable is first assigned/used in
        // scope 0 = function scope, incrementing for each IRLabel
        int currentScope = 0;
        var varFirstScope = new Dictionary<string, int>();

        foreach (var instr in instructions)
        {
            if (instr is IRLabel)
            {
                currentScope++;
                continue;
            }

            var code = instr.ToCpp();
            // Find all __tN references in the code
            for (int i = 0; i < code.Length - 3; i++)
            {
                if (code[i] == '_' && code[i + 1] == '_' && code[i + 2] == 't'
                    && i + 3 < code.Length && char.IsDigit(code[i + 3]))
                {
                    // Check we're not inside __this, __type_info, etc.
                    if (i > 0 && char.IsLetterOrDigit(code[i - 1])) continue;

                    // Extract full variable name __tN
                    int end = i + 3;
                    while (end < code.Length && char.IsDigit(code[end])) end++;
                    // Make sure it's not followed by more identifier chars
                    if (end < code.Length && (char.IsLetter(code[end]) || code[end] == '_')) continue;

                    var varName = code[i..end];
                    if (varFirstScope.TryGetValue(varName, out var firstScope))
                    {
                        if (firstScope != currentScope)
                            result.Add(varName);
                    }
                    else
                    {
                        varFirstScope[varName] = currentScope;
                    }
                }
            }
        }
        return result;
    }

    /// <summary>
    /// Determine the C++ type of each __tN temp variable by scanning all IR instructions.
    /// Returns a map from variable name to its C++ type (e.g., "__t0" → "System_IO_TextWriter*").
    /// </summary>
    private static Dictionary<string, string> DetermineTempVarTypes(List<IRInstruction> instructions, IRMethod? irMethod = null)
    {
        var types = new Dictionary<string, string>();
        // Primary source: authoritative types recorded during IL→IR conversion
        if (irMethod?.TempVarTypes is { Count: > 0 } authTypes)
        {
            foreach (var (name, type) in authTypes)
                types[name] = type;
        }
        foreach (var instr in instructions)
        {
            switch (instr)
            {
                case IRCall call when call.ResultVar != null && call.ResultTypeCpp != null:
                    types.TryAdd(call.ResultVar, call.ResultTypeCpp);
                    break;
                case IRNewObj newObj when newObj.ResultVar != null:
                    types.TryAdd(newObj.ResultVar, newObj.TypeCppName + "*");
                    break;
                case IRFieldAccess fa when !fa.IsStore && fa.ResultVar != null && fa.ResultTypeCpp != null:
                    types.TryAdd(fa.ResultVar, fa.ResultTypeCpp);
                    break;
                case IRStaticFieldAccess sfa when !sfa.IsStore && sfa.ResultVar != null && sfa.ResultTypeCpp != null:
                    types.TryAdd(sfa.ResultVar, sfa.ResultTypeCpp);
                    break;
                case IRCast cast when cast.ResultVar != null:
                    types.TryAdd(cast.ResultVar, cast.TargetTypeCpp);
                    break;
                case IRConversion conv when conv.ResultVar != null:
                    types.TryAdd(conv.ResultVar, conv.TargetType);
                    break;
                case IRBox box when box.ResultVar != null:
                    types.TryAdd(box.ResultVar, "cil2cpp::Object*");
                    break;
                case IRUnbox unbox when unbox.ResultVar != null:
                    types.TryAdd(unbox.ResultVar, unbox.IsUnboxAny ? unbox.ValueTypeCppName : unbox.ValueTypeCppName + "*");
                    break;
                case IRArrayAccess aa when !aa.IsStore && aa.ResultVar != null:
                    types.TryAdd(aa.ResultVar, aa.ElementType);
                    break;
                case IRLoadFunctionPointer lfp when lfp.ResultVar != null:
                    types.TryAdd(lfp.ResultVar, "void*");
                    break;
                case IRDelegateCreate dc when dc.ResultVar != null:
                    types.TryAdd(dc.ResultVar, "cil2cpp::Delegate*");
                    break;
                case IRDelegateInvoke di when di.ResultVar != null:
                    types.TryAdd(di.ResultVar, di.ReturnTypeCpp);
                    break;
                case IRRawCpp raw when raw.ResultVar != null && raw.ResultTypeCpp != null:
                    types.TryAdd(raw.ResultVar, raw.ResultTypeCpp);
                    break;
                case IRUnaryOp unOp when unOp.ResultVar.StartsWith("__t") && unOp.ResultTypeCpp != null:
                    types.TryAdd(unOp.ResultVar, unOp.ResultTypeCpp);
                    break;
                // IRDeclareLocal for value type newobj: "StructType __tN = {0};"
                // Must record the type so cross-scope pre-declaration uses the correct type
                // instead of defaulting to Object*.
                case IRDeclareLocal decl when decl.VarName.StartsWith("__t"):
                    types.TryAdd(decl.VarName, decl.TypeName);
                    break;
                // IRBinaryOp: comparisons produce bool, arithmetic produces intptr_t
                case IRBinaryOp binOp when binOp.ResultVar.StartsWith("__t"):
                {
                    // ECMA-335: comparisons produce int32 (0/1), not bool.
                    // ToCpp() casts comparison results to (int32_t) to match.
                    var resultType = binOp.Op is "==" or "!=" or "<" or ">" or "<=" or ">="
                        ? "int32_t" : "intptr_t";
                    types.TryAdd(binOp.ResultVar, resultType);
                    break;
                }
                // Fallback: infer type from IRRawCpp code patterns
                case IRRawCpp raw2 when raw2.ResultVar == null || raw2.ResultTypeCpp == null:
                    InferTypeFromRawCpp(raw2.Code, types);
                    break;
            }
        }
        return types;
    }

    /// <summary>
    /// Infer temp variable types from IRRawCpp code patterns.
    /// Handles cases where ResultVar/ResultTypeCpp aren't set on the instruction.
    /// </summary>
    private static void InferTypeFromRawCpp(string code, Dictionary<string, string> types)
    {
        // Match: __tN = (type)expr; or __tN = (type*)expr;
        if (!code.StartsWith("__t") || code.StartsWith("__this")) return;
        var eqIdx = code.IndexOf(" = ");
        if (eqIdx <= 0) return;
        var varName = code[..eqIdx].Trim();
        if (!varName.StartsWith("__t") || !varName.Skip(3).All(char.IsDigit)) return;
        if (types.ContainsKey(varName)) return;

        var rhs = code[(eqIdx + 3)..].TrimEnd(';').Trim();

        // (type)expr or (type*)expr — extract the cast type
        if (rhs.StartsWith("(") && !rhs.StartsWith("(&"))
        {
            var closeIdx = rhs.IndexOf(')');
            if (closeIdx > 1)
            {
                var castType = rhs[1..closeIdx];
                // Only accept simple type names (no nested parens, no operators)
                if (!castType.Contains("(") && !castType.Contains(" ") || castType.EndsWith("*"))
                {
                    types.TryAdd(varName, castType);
                    return;
                }
            }
        }

        // Comparison operators → bool
        if (rhs.Contains(" == ") || rhs.Contains(" != ") ||
            rhs.Contains(" < ") || rhs.Contains(" > ") ||
            rhs.Contains(" <= ") || rhs.Contains(" >= "))
        {
            types.TryAdd(varName, "bool");
            return;
        }

        // Arithmetic/bitwise operators on non-pointer values → int32_t (safe default)
        if ((rhs.Contains(" + ") || rhs.Contains(" - ") || rhs.Contains(" * ") ||
             rhs.Contains(" / ") || rhs.Contains(" % ") || rhs.Contains(" | ") ||
             rhs.Contains(" & ") || rhs.Contains(" ^ ") || rhs.Contains(" << ") ||
             rhs.Contains(" >> ")) && !rhs.Contains("->") && !rhs.Contains("*)"))
        {
            types.TryAdd(varName, "intptr_t"); // intptr_t is safe for both 32/64-bit arithmetic
            return;
        }

        // Negation: -expr → numeric
        if (rhs.StartsWith("-") && !rhs.Contains("->"))
        {
            types.TryAdd(varName, "intptr_t");
            return;
        }

        // ~expr (bitwise NOT) → numeric
        if (rhs.StartsWith("~"))
        {
            types.TryAdd(varName, "intptr_t");
            return;
        }
    }

    /// <summary>
    /// Replace "__tN = expr" with "auto __tN = expr" on first use of temp vars.
    /// Only matches pure temp variables like __t0, __t1, etc.
    /// </summary>
    private string AddAutoDeclarations(string code, HashSet<string> declaredTemps)
    {
        // Null-check prefix: "cil2cpp::null_check(...); __tN = ..."
        // Split into prefix and assignment, then add 'auto' to the assignment part.
        const string nullCheckPrefix = "cil2cpp::null_check(";
        if (code.StartsWith(nullCheckPrefix))
        {
            var splitIdx = code.IndexOf("; __t");
            if (splitIdx > 0)
            {
                var prefix = code[..(splitIdx + 2)]; // "cil2cpp::null_check(...); "
                var rest = code[(splitIdx + 2)..];    // "__tN = ..."
                var declared = AddAutoDeclarations(rest, declaredTemps);
                return prefix + declared;
            }
            return code;
        }

        // Match patterns like "__t0 = ..." but NOT "__this->..."
        if (code.StartsWith("__t") && !code.StartsWith("__this"))
        {
            var eqIdx = code.IndexOf(" = ");
            if (eqIdx > 0)
            {
                var varName = code[..eqIdx].Trim();
                // Must be a simple temp var name like __t0, __t1, etc.
                if (varName.StartsWith("__t") &&
                    varName.All(c => c == '_' || char.IsLetterOrDigit(c)) &&
                    !declaredTemps.Contains(varName))
                {
                    declaredTemps.Add(varName);
                    // Check if RHS of the FIRST line references the same variable (self-referencing
                    // initializer). e.g., "__t16 = __t16 + 5" → "auto __t16 = __t16 + 5" is C++ UB.
                    // Only check first line — multi-line instructions (IRNewObj) have subsequent
                    // lines that reference the variable as arguments, which is NOT self-referencing.
                    var rhs = code[(eqIdx + 3)..];
                    var newlineIdx = rhs.IndexOf('\n');
                    var firstLineRhs = newlineIdx >= 0 ? rhs[..newlineIdx] : rhs;
                    if (ContainsVarAtWordBoundary(firstLineRhs, varName))
                        return code;
                    return $"auto {code}";
                }
            }
        }
        return code;
    }

    /// <summary>
    /// Check if a string contains a variable name at word boundaries.
    /// __t0 matches in "__t0 + 5" but NOT in "__t0_statics" or "__t0something".
    /// </summary>
    private static bool ContainsVarAtWordBoundary(string text, string varName)
    {
        int idx = 0;
        while ((idx = text.IndexOf(varName, idx, StringComparison.Ordinal)) >= 0)
        {
            int after = idx + varName.Length;
            if (after >= text.Length || !char.IsLetterOrDigit(text[after]) && text[after] != '_')
                return true;
            idx = after;
        }
        return false;
    }

    /// <summary>
    /// Wrap plain assignments to cross-scope pointer variables with explicit (void*) casts.
    /// Different IL branches may assign different pointer types to the same temp variable.
    /// Without explicit casts, C++ rejects the assignment because generated flat structs
    /// don't have C++ inheritance (all are unrelated struct types).
    /// Example: "__t5 = __t3;" → "__t5 = (System_Exception*)(void*)__t3;"
    /// </summary>
    private static string WrapCrossScopePointerAssignment(
        string code, Dictionary<string, string> crossScopePtrVars)
    {
        // Handle null_check prefix: "cil2cpp::null_check(...); __tN = ..."
        const string nullCheckPrefix = "cil2cpp::null_check(";
        if (code.StartsWith(nullCheckPrefix))
        {
            var splitIdx = code.IndexOf("; __t");
            if (splitIdx > 0)
            {
                var prefix = code[..(splitIdx + 2)]; // "cil2cpp::null_check(...); "
                var rest = code[(splitIdx + 2)..];
                var wrapped = WrapCrossScopePointerAssignment(rest, crossScopePtrVars);
                return (wrapped != rest) ? prefix + wrapped : code;
            }
            return code;
        }

        // Only handle assignments to temp variables
        if (!code.StartsWith("__t") || code.StartsWith("__this")) return code;

        var eqIdx = code.IndexOf(" = ");
        if (eqIdx < 0) return code;

        var varName = code[..eqIdx];
        if (!crossScopePtrVars.TryGetValue(varName, out var preType)) return code;

        // Already has the correct target type cast (e.g., (String*)(void*)expr) — no wrapping needed
        if (code.Contains($"({preType})(void*)")) return code;

        var rhs = code[(eqIdx + 3)..].TrimEnd(';').Trim();

        // intptr_t/uintptr_t pre-declared vars: in IL, native int and pointers are freely
        // interchangeable. When a pointer (uint8_t*, void*, Object*, etc.) is assigned to
        // an intptr_t/uintptr_t variable, wrap with a C-style cast.
        if (preType is "intptr_t" or "uintptr_t")
        {
            // Already the correct type or already cast
            if (rhs.StartsWith($"({preType})")) return code;
            // Simple variable, function call, or expression — wrap with C-style cast
            // Don't wrap if rhs is already intptr_t/uintptr_t compatible (numeric literal, etc.)
            // Only wrap when rhs looks like a pointer expression (loc_N, __tN, function call)
            if (rhs.StartsWith("loc_") || rhs.StartsWith("__t") || rhs.StartsWith("(uint8_t*)")
                || rhs.StartsWith("(void*)") || rhs.StartsWith("(cil2cpp::"))
                return $"{varName} = ({preType}){rhs};";
            return code;
        }

        // Has (void*) but missing target type — conv.u erased the type.
        // e.g., __t5 = (void*)__t8; → __t5 = (cil2cpp::String*)(void*)__t8;
        if (code.Contains("(void*)"))
        {
            if (rhs.StartsWith("(void*)"))
                return $"{varName} = ({preType}){rhs};";
            return code;
        }

        // Don't wrap nullptr (compatible with any pointer type)
        if (rhs == "nullptr") return code;

        // Don't wrap ternary expressions or arithmetic
        if (rhs.Contains('?') || rhs.Contains('+') || rhs.Contains('-') && !rhs.StartsWith("-"))
            return code;

        // For simple expressions (__tM, __tM->field, loc_N): wrap directly
        // For function calls (FuncName(args)): wrap the whole expression
        // Wrap: __tN = expr → __tN = (PreType)(void*)expr
        // Parentheses in function calls bind tighter than (void*) cast, so
        // (PreType)(void*)FuncName(args) evaluates as (PreType)(void*)(FuncName(args)) — correct.
        return $"{varName} = ({preType})(void*){rhs};";
    }

