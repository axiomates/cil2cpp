using System.Text;
using CIL2CPP.Core.IL;
using CIL2CPP.Core.IR;

namespace CIL2CPP.Core.CodeGen;

public partial class CppCodeGenerator
{
    /// <summary>
    /// Emit common source file header (includes, assembly info).
    /// </summary>
    private void EmitSourceFileHeader(StringBuilder sb, string description)
    {
        sb.AppendLine("// Generated by CIL2CPP - DO NOT EDIT");
        sb.AppendLine($"// Source assembly: {_module.Name}");
        sb.AppendLine($"// {description}");
        if (_config.IsDebug)
            sb.AppendLine("// DEBUG BUILD - contains #line directives and IL offset comments");
        sb.AppendLine();
        sb.AppendLine($"#include \"{_module.Name}.h\"");
        sb.AppendLine();
        sb.AppendLine("#include <cstdio>");
        sb.AppendLine("#include <cmath>");
        sb.AppendLine("#include <cstring>");
        sb.AppendLine("#include <algorithm>");
        sb.AppendLine("#include <limits>");
        sb.AppendLine("#include <atomic>");
        sb.AppendLine();
    }

    /// <summary>
    /// Generate the data file: string literals, array init data, static fields,
    /// TypeInfo, VTable, interface data, reflection metadata, ensure_cctor, P/Invoke.
    /// </summary>
    private GeneratedFile GenerateDataFile()
    {
        var sb = new StringBuilder();
        EmitSourceFileHeader(sb, "Data: metadata, TypeInfo, static fields, VTable, P/Invoke");

        // String literals (non-static — referenced from method files via extern in header)
        if (_module.StringLiterals.Count > 0)
        {
            sb.AppendLine("// ===== String Literals =====");
            foreach (var (value, literal) in _module.StringLiterals)
            {
                sb.AppendLine($"cil2cpp::String* {literal.Id} = nullptr;");
            }
            sb.AppendLine();

            sb.AppendLine("void __init_string_literals() {");
            foreach (var (value, literal) in _module.StringLiterals)
            {
                if (value.Contains('\0'))
                {
                    // Strings with embedded NUL bytes can't use NUL-terminated C string APIs.
                    // Emit as raw UTF-16 data array with explicit length.
                    var utf16Hex = string.Join(", ", value.Select(c => $"0x{(int)c:X4}"));
                    sb.AppendLine($"    {{ static const uint16_t d[] = {{ {utf16Hex} }}; {literal.Id} = cil2cpp::string_literal_utf16((cil2cpp::Char*)d, {value.Length}); }}");
                }
                else
                {
                    var escaped = EscapeString(value);
                    sb.AppendLine($"    {literal.Id} = cil2cpp::string_literal(\"{escaped}\");");
                }
            }
            sb.AppendLine("}");
            sb.AppendLine();
        }

        // Array initializer data blobs (non-static — referenced from method files)
        if (_module.ArrayInitDataBlobs.Count > 0)
        {
            sb.AppendLine("// ===== Array Initializer Data =====");
            foreach (var blob in _module.ArrayInitDataBlobs)
            {
                var bytes = string.Join(", ", blob.Data.Select(b => $"0x{b:X2}"));
                sb.AppendLine($"const unsigned char {blob.Id}[] = {{ {bytes} }};");
            }
            sb.AppendLine();
        }

        // Static field storage (include RuntimeProvided types — BCL code uses their statics)
        foreach (var type in _userTypes)
        {
            if (type.IsEnum || type.IsDelegate) continue;
            if (type.StaticFields.Count > 0)
            {
                sb.AppendLine($"{type.CppName}_Statics {type.CppName}_statics = {{}};");
            }
        }
        if (_userTypes.Any(t => !t.IsEnum && !t.IsDelegate && t.StaticFields.Count > 0))
        {
            sb.AppendLine();
        }

        // Primitive type TypeInfo definitions (for array element types)
        // Runtime-provided reference types get aliases to runtime TypeInfo instead of new definitions.
        if (_module.PrimitiveTypeInfos.Count > 0)
        {
            sb.AppendLine("// ===== Primitive Type Info (array element types) =====");
            foreach (var entry in _module.PrimitiveTypeInfos.Values)
            {
                // Runtime-provided reference types: alias to runtime's TypeInfo (e.g., cil2cpp::System::Object_TypeInfo)
                var runtimeAlias = RuntimeTypeRegistry.GetRuntimeTypeInfoName(entry.ILFullName);
                if (runtimeAlias != null)
                {
                    sb.AppendLine($"cil2cpp::TypeInfo& {entry.CppMangledName}_TypeInfo = {runtimeAlias};");
                    continue;
                }

                // System.Void has no sizeof — use 0 for instance/element sizes
                var sizeExpr = entry.ILFullName == "System.Void" ? "0" : $"sizeof({entry.CppTypeName})";
                sb.AppendLine($"cil2cpp::TypeInfo {entry.CppMangledName}_TypeInfo = {{");
                sb.AppendLine($"    .name = \"{entry.ILFullName.Split('.').Last()}\",");
                sb.AppendLine($"    .namespace_name = \"{entry.ILFullName[..entry.ILFullName.LastIndexOf('.')]}\",");
                sb.AppendLine($"    .full_name = \"{entry.ILFullName}\",");
                sb.AppendLine($"    .base_type = nullptr,");
                sb.AppendLine($"    .interfaces = nullptr,");
                sb.AppendLine($"    .interface_count = 0,");
                sb.AppendLine($"    .instance_size = {sizeExpr},");
                sb.AppendLine($"    .element_size = {sizeExpr},");
                // Primitive types get both ValueType and Primitive flags (ECMA-335)
                sb.AppendLine($"    .flags = cil2cpp::TypeFlags::ValueType | cil2cpp::TypeFlags::Primitive,");
                sb.AppendLine($"    .vtable = nullptr,");
                sb.AppendLine($"    .fields = nullptr,");
                sb.AppendLine($"    .field_count = 0,");
                sb.AppendLine($"    .methods = nullptr,");
                sb.AppendLine($"    .method_count = 0,");
                sb.AppendLine($"    .default_ctor = nullptr,");
                sb.AppendLine($"    .finalizer = nullptr,");
                sb.AppendLine($"    .interface_vtables = nullptr,");
                sb.AppendLine($"    .interface_vtable_count = 0,");
                sb.AppendLine($"    .custom_attributes = nullptr,");
                sb.AppendLine($"    .custom_attribute_count = 0,");
                var primCorType = GetCorElementTypeForPrimitive(entry.ILFullName);
                sb.AppendLine($"    .cor_element_type = 0x{primCorType:X2},");
                sb.AppendLine($"    .underlying_type = nullptr,");
                sb.AppendLine($"}};");
            }
            sb.AppendLine();
        }

        // Exception TypeInfo reference aliases — define references to runtime-declared TypeInfos
        // so that generated catch/cast code can use mangled names (System_Exception_TypeInfo)
        sb.AppendLine("// ===== Exception TypeInfo Aliases =====");
        foreach (var (mangledName, runtimeTypeInfoName) in RuntimeTypeRegistry.GetExceptionTypeInfoAliases())
        {
            sb.AppendLine($"cil2cpp::TypeInfo& {mangledName}_TypeInfo = {runtimeTypeInfoName};");
        }
        sb.AppendLine();

        // Ensure System_Object_TypeInfo and System_String_TypeInfo exist at global scope
        // (normally defined by PrimitiveTypeInfos; alias as fallback if missing)
        var needsObjectAlias = !_module.PrimitiveTypeInfos.Values.Any(e => e.ILFullName == "System.Object");
        var needsStringAlias = !_module.PrimitiveTypeInfos.Values.Any(e => e.ILFullName == "System.String");
        if (needsObjectAlias || needsStringAlias)
        {
            sb.AppendLine("// ===== Runtime TypeInfo Aliases =====");
            if (needsObjectAlias)
                sb.AppendLine("cil2cpp::TypeInfo& System_Object_TypeInfo = cil2cpp::System_Object_TypeInfo;");
            if (needsStringAlias)
                sb.AppendLine("cil2cpp::TypeInfo& System_String_TypeInfo = cil2cpp::System_String_TypeInfo;");
            sb.AppendLine();
        }

        // Stub TypeInfos for runtime-provided base types not declared in the runtime library
        // These are referenced as .base_type in TypeInfo initialization for value types, enums, delegates
        sb.AppendLine("// ===== Runtime Base Type TypeInfo Stubs =====");
        foreach (var (mangledName, ilName) in RuntimeTypeRegistry.GetBaseTypeStubs())
        {
            sb.AppendLine($"cil2cpp::TypeInfo {mangledName}_TypeInfo = {{ " +
                $".name = \"{ilName.Split('.').Last()}\", " +
                $".namespace_name = \"{string.Join(".", ilName.Split('.').SkipLast(1))}\", " +
                $".full_name = \"{ilName}\", " +
                $".base_type = &System_Object_TypeInfo }};");
        }
        sb.AppendLine();

        // Unboxing thunks for value type methods (must come before vtable/interface data)
        EmitUnboxThunks(sb, _userTypes);

        // VTable data
        EmitVTableData(sb, _userTypes);

        // Interface data
        EmitInterfaceData(sb, _userTypes);

        // Finalizer wrappers
        EmitFinalizerWrappers(sb, _userTypes);

        // Reflection metadata (FieldInfo/MethodInfo arrays)
        EmitReflectionMetadata(sb, _userTypes);

        // Generic variance data arrays (for variance-aware type checking)
        EmitGenericVarianceData(sb, _userTypes);

        // Type info definitions (skip runtime-provided types — already defined in runtime)
        sb.AppendLine("// ===== Type Info =====");
        var emittedTypeInfo = new HashSet<string>();
        // Collect types that already have TypeInfo definitions (primitives, exceptions, base type stubs, aliases)
        foreach (var entry in _module.PrimitiveTypeInfos.Values)
            emittedTypeInfo.Add(entry.CppMangledName);
        foreach (var (mangledName, _) in RuntimeTypeRegistry.GetExceptionTypeInfoAliases())
            emittedTypeInfo.Add(mangledName);
        foreach (var (mangledName, _) in RuntimeTypeRegistry.GetBaseTypeStubs())
            emittedTypeInfo.Add(mangledName);
        // Runtime TypeInfo aliases (Object, String) — already emitted as references above
        if (needsObjectAlias) emittedTypeInfo.Add("System_Object");
        if (needsStringAlias) emittedTypeInfo.Add("System_String");
        foreach (var type in _userTypes)
        {
            if (emittedTypeInfo.Contains(type.CppName)) continue;
            if (!IsValidCppIdentifier(type.CppName)) continue;
            if (!emittedTypeInfo.Add(type.CppName)) continue;
            if (type.IsRuntimeProvided && IRBuilder.CoreRuntimeTypes.Contains(type.ILFullName))
            {
                // Core runtime types with vtable entries (constructed, used for virtual dispatch)
                // need full TypeInfo so runtime-created instances can dispatch correctly.
                if (type.VTable.Count > 0)
                {
                    GenerateTypeInfo(sb, type);
                    continue;
                }
                // Emit minimal TypeInfo stub for core runtime types without vtables
                // (other types may reference these as base_type)
                var baseName = type.BaseType != null && emittedTypeInfo.Contains(type.BaseType.CppName)
                    ? $"&{type.BaseType.CppName}_TypeInfo" : "&System_Object_TypeInfo";
                sb.AppendLine($"cil2cpp::TypeInfo {type.CppName}_TypeInfo = {{ .name = \"{type.Name}\", " +
                    $".namespace_name = \"{type.Namespace}\", .full_name = \"{type.ILFullName}\", " +
                    $".base_type = {baseName} }};");
                continue;
            }
            if (type.IsRuntimeProvided && !IRBuilder.CoreRuntimeTypes.Contains(type.ILFullName))
            {
                // Non-core runtime-provided types (Task, Thread, etc.): struct layout from runtime,
                // but methods compiled from BCL IL — need full TypeInfo with vtable
                GenerateTypeInfo(sb, type);
                continue;
            }
            GenerateTypeInfo(sb, type);
        }
        // External enum TypeInfo definitions — minimal stubs for BCL enums referenced in
        // generic specializations (e.g., Enum.TryFormatUnconstrained<ExceptionHandlingClauseOptions>)
        foreach (var (mangledName, underlyingType) in _module.ExternalEnumTypes)
        {
            if (emittedTypeInfo.Contains(mangledName)) continue;
            // Extract name/namespace from mangled name (e.g., "System_Reflection_ExceptionHandlingClauseOptions")
            var parts = mangledName.Split('_');
            var ns = parts.Length > 2 ? string.Join(".", parts[..^1]) : "";
            var name = parts.Length > 0 ? parts[^1] : mangledName;
            sb.AppendLine($"cil2cpp::TypeInfo {mangledName}_TypeInfo = {{ .name = \"{name}\", " +
                $".namespace_name = \"{ns}\", .full_name = \"{mangledName}\", " +
                $".base_type = &System_Enum_TypeInfo, " +
                $".flags = cil2cpp::TypeFlags::ValueType | cil2cpp::TypeFlags::Enum }};");
            emittedTypeInfo.Add(mangledName);
        }
        // Auto-discovered TypeInfo definitions — types referenced as _TypeInfo in method bodies
        // but not present in userTypes or other TypeInfo sources.
        // Build CppName → ILFullName lookup for correct full_name in TypeInfo
        var cppToIL = new Dictionary<string, string>();
        foreach (var type in _module.Types)
            cppToIL.TryAdd(type.CppName, type.ILFullName);
        // Also include ldtoken-recorded mappings (open generic types not in module Types)
        foreach (var (cppName, ilName) in _module.TypeInfoCppToILMap)
            cppToIL.TryAdd(cppName, ilName);
        foreach (var typeName in _autoTypeInfoDecls)
        {
            if (emittedTypeInfo.Contains(typeName)) continue;
            // Use ILFullName if available, otherwise fall back to CppName
            var ilFullName = cppToIL.GetValueOrDefault(typeName, typeName);
            // Extract name part (last segment) and namespace (everything before)
            var lastSep = typeName.LastIndexOf('_');
            var name = lastSep >= 0 ? typeName[(lastSep + 1)..] : typeName;
            // Build fields from metadata if available
            var flagsPart = "";
            var basePart = "";
            var nsPart = "";
            if (_module.TypeInfoAutoMetadata.TryGetValue(typeName, out var meta))
            {
                var flags = new List<string>();
                if (meta.IsInterface) flags.Add("cil2cpp::TypeFlags::Interface");
                if (meta.IsAbstract) flags.Add("cil2cpp::TypeFlags::Abstract");
                if (meta.IsSealed) flags.Add("cil2cpp::TypeFlags::Sealed");
                if (meta.IsValueType) flags.Add("cil2cpp::TypeFlags::ValueType");
                if (meta.IsEnum) flags.Add("cil2cpp::TypeFlags::Enum");
                if (meta.IsPublic) flags.Add("cil2cpp::TypeFlags::Public");
                if (flags.Count > 0)
                    flagsPart = $", .flags = {string.Join(" | ", flags)}";
                // base_type: needed for IsSubclassOf (e.g., Attribute.GetCustomAttributes checks)
                // Only emit if the base type's TypeInfo is actually declared (skip open generic types etc.)
                if (meta.BaseTypeCppName != null &&
                    (emittedTypeInfo.Contains(meta.BaseTypeCppName) || _autoTypeInfoDecls.Contains(meta.BaseTypeCppName)))
                    basePart = $", .base_type = &{meta.BaseTypeCppName}_TypeInfo";
                if (meta.NamespaceName != null)
                    nsPart = $", .namespace_name = \"{meta.NamespaceName}\"";
            }
            sb.AppendLine($"cil2cpp::TypeInfo {typeName}_TypeInfo = {{ .name = \"{name}\"{nsPart}, " +
                $".full_name = \"{ilFullName}\"{basePart}{flagsPart} }};");
            emittedTypeInfo.Add(typeName);
        }
        sb.AppendLine();

        // Static constructor guards — collect all types that SHOULD have ensure_cctor
        // (matches the header declaration loop in GenerateHeader)
        var needsEnsureCctor = new HashSet<string>();
        foreach (var type in _userTypes)
        {
            if (type.HasCctor)
                needsEnsureCctor.Add(type.CppName);
        }
        var emittedEnsureCctor = new HashSet<string>();
        foreach (var type in _userTypes)
        {
            if (type.HasCctor)
            {
                var cctorMethod = type.Methods.FirstOrDefault(m => m.IsStaticConstructor);
                if (cctorMethod != null)
                {
                    // Types discovered late (Pass 6.1b+) may have HasCctor=true but cctor body
                    // not compiled (CLR-internal dependencies or unreachable). Method bodies already
                    // reference ensure_cctor, so emit a no-op to satisfy the linker. This is
                    // architecturally correct: the cctor would initialize CLR-internal state
                    // irrelevant in AOT.
                    if (cctorMethod.BasicBlocks.Count == 0)
                    {
                        sb.AppendLine($"void {type.CppName}_ensure_cctor() {{ }}");
                        emittedEnsureCctor.Add(type.CppName);
                        continue;
                    }
                    sb.AppendLine($"static bool {type.CppName}_cctor_called = false;");
                    sb.AppendLine($"void {type.CppName}_ensure_cctor() {{");
                    sb.AppendLine($"    if (!{type.CppName}_cctor_called) {{");
                    sb.AppendLine($"        {type.CppName}_cctor_called = true;");
                    sb.AppendLine($"        {cctorMethod.CppName}();");
                    sb.AppendLine($"    }}");
                    sb.AppendLine($"}}");
                    emittedEnsureCctor.Add(type.CppName);
                    sb.AppendLine();
                }
                else
                {
                    sb.AppendLine($"void {type.CppName}_ensure_cctor() {{ }}");
                    emittedEnsureCctor.Add(type.CppName);
                }
            }
        }
        // Safety net: emit no-op for any ensure_cctor declared in the header but not generated.
        // This handles cases where CppName dedup in source drops a type with HasCctor.
        foreach (var type in _module.Types)
        {
            if (type.HasCctor && !emittedEnsureCctor.Contains(type.CppName))
            {
                sb.AppendLine($"void {type.CppName}_ensure_cctor() {{ }}");
                emittedEnsureCctor.Add(type.CppName);
            }
        }

        // P/Invoke extern declarations and wrappers
        EmitPInvokeDeclarations(sb, _userTypes, _emittedMethodSignatures);

        // Opaque Span/ReadOnlySpan stub method implementations
        // These types don't have IRType objects but need trivial get_Length/get_IsEmpty methods.
        if (_opaqueSpanStubs.Count > 0)
        {
            sb.AppendLine("// ===== Opaque Span Stub Method Implementations =====");
            foreach (var spanType in _opaqueSpanStubs)
            {
                var lenSig = $"int32_t {spanType}_get_Length({spanType}* __this)";
                var emptySig = $"bool {spanType}_get_IsEmpty({spanType}* __this)";
                sb.AppendLine($"{lenSig} {{ return __this->f__length; }}");
                sb.AppendLine($"{emptySig} {{ return __this->f__length == 0; }}");
                // Register as emitted so stubs generator doesn't duplicate them
                _emittedMethodSignatures.Add(lenSig);
                _emittedMethodSignatures.Add(emptySig);
            }
            sb.AppendLine();
        }

        // Runtime init: Patch runtime built-in TypeInfos with codegen vtable/interfaces.
        // Runtime has TWO TypeInfos for String/Object:
        //   1. cil2cpp::System::String_TypeInfo (BCL, used by string_literal/string_create_utf8)
        //   2. cil2cpp::System_String_TypeInfo (reflection, used by codegen reference alias)
        // Both have vtable=nullptr at startup. Codegen patches both so virtual dispatch works
        // on strings regardless of which TypeInfo pointer they carry.
        // This is standard IL2CPP architecture — runtime provides struct layout, codegen provides vtables.
        EmitRuntimeVTablePatching(sb, needsStringAlias, needsObjectAlias);

        return new GeneratedFile
        {
            FileName = $"{_module.Name}_data.cpp",
            Content = sb.ToString()
        };
    }

    /// <summary>
    /// Generate method implementation files, partitioned for parallel compilation.
    /// Two-phase approach:
    ///   Phase 1 (sequential): Filter methods per partition — handles signature dedup,
    ///           undeclared-call checks, and diagnostic counters (shared mutable state).
    ///   Phase 2 (parallel): Generate C++ code per partition — pure string construction,
    ///           no shared mutable state (deadCodeReplacements collected per-partition and merged).
    /// </summary>
    private List<GeneratedFile> GenerateMethodFiles()
    {
        var partitions = PartitionTypes(_userTypes);

        // Phase 1: Sequential filtering — collect methods to emit per partition.
        var partitionMethods = new List<IRMethod>[partitions.Count];
        for (int i = 0; i < partitions.Count; i++)
        {
            partitionMethods[i] = new List<IRMethod>();
            foreach (var type in partitions[i])
                FilterMethodsForType(type, partitionMethods[i]);
        }

        // Phase 2: Parallel code generation — each partition builds its own StringBuilder.
        var results = new (string FileName, string Content, List<(string, string, string)> DeadCode)[partitions.Count];
        Parallel.For(0, partitions.Count, i =>
        {
            var localDeadCode = new List<(string Category, string FunctionName, string InMethod)>();
            var sb = new StringBuilder();
            EmitSourceFileHeader(sb, $"Methods (part {i + 1} of {partitions.Count})");

            sb.AppendLine("// ===== Method Implementations =====");
            foreach (var method in partitionMethods[i])
                GenerateMethodImpl(sb, method, localDeadCode);

            results[i] = ($"{_module.Name}_methods_{i}.cpp", sb.ToString(), localDeadCode);
        });

        // Merge per-partition dead-code diagnostics into the shared list.
        var files = new List<GeneratedFile>(partitions.Count);
        for (int i = 0; i < results.Length; i++)
        {
            files.Add(new GeneratedFile { FileName = results[i].FileName, Content = results[i].Content });
            _deadCodeReplacements.AddRange(results[i].DeadCode);
        }

        return files;
    }

    /// <summary>
    /// Filter methods from a type that should be emitted. Sequential — writes to shared
    /// _emittedMethodSignatures and _skippedBy* diagnostic lists.
    /// </summary>
    private void FilterMethodsForType(IRType type, List<IRMethod> output)
    {
        if (type.IsDelegate) return;

        // For interfaces, only emit DIM method bodies (non-abstract with converted bodies)
        if (type.IsInterface)
        {
            foreach (var method in type.Methods)
            {
                if (method.IsAbstract || method.BasicBlocks.Count == 0) continue;
                if (HasInvalidCppSignature(method)) continue;
                if (!_emittedMethodSignatures.Add(method.GetCppSignature())) continue;
                output.Add(method);
            }
            return;
        }

        // All types (user + BCL) compile from IL — Unity IL2CPP model
        foreach (var method in type.Methods)
        {
            if (method.IsInternalCall || method.IsPInvoke) continue;
            if (IRBuilder.SkipAllMethodsTypes.Contains(type.ILFullName)) continue;
            // CoreRuntimeTypes: selectively emit methods that have real IL-compiled bodies.
            // Methods that couldn't compile (CLR-internal deps → IrStubReason set) are
            // left to core_methods.cpp. Types with aliased struct layouts (reflection types,
            // Thread, RuntimeType) keep the blanket gate since IL field accesses don't match
            // the C++ runtime struct fields.
            if (!method.IsStatic && IRBuilder.CoreRuntimeTypes.Contains(type.ILFullName))
            {
                if (RuntimeTypeRegistry.ShouldBlockInstanceMethod(type.ILFullName, method))
                    continue;
            }
            if (method.IsAbstract) continue;
            if (method.BasicBlocks.Count == 0) continue;
            if (method.HasICallMapping) continue;
            if (HasInvalidCppSignature(method))
            {
                _skippedByInvalidSignature.Add((type.ILFullName, method.CppName));
                continue;
            }
            if (HasGenericBodyTypeConflict(type, method))
            {
                _skippedByGenericBodyConflict.Add((type.ILFullName, method.CppName));
                continue;
            }
            // Skip methods that are themselves SIMD dead code.
            // Their callers have been replaced with default values at render time;
            // emitting the method body would reference SIMD opaque struct fields.
            // Checked before CallsUndeclaredFunction to avoid miscounting SIMD methods
            // as undeclared-function skips.
            if (IsSimdDeadCodeFunction(method.CppName))
                continue;
            var undeclaredCall = FindFirstUndeclaredCall(method);
            if (undeclaredCall != null)
            {
                _skippedByUndeclaredFunction.Add((type.ILFullName, method.CppName, undeclaredCall));
                continue;
            }
            if (!_emittedMethodSignatures.Add(method.GetCppSignature())) continue;
            // Direct render — compile from IL
            output.Add(method);
        }
    }

