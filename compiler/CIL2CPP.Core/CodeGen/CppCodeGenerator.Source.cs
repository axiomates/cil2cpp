    /// <summary>
    /// Generate a typed function pointer cast expression for a method, to resolve overload ambiguity.
    /// E.g., (void*)(void(*)(Foo*, int32_t))&Foo_Bar
    /// </summary>
    private string GetTypedMethodPointerCast(IRMethod method)
    {
        if (method.IsAbstract || method.BasicBlocks.Count == 0)
            return "nullptr";

        return BuildMethodPointerCast(method);
    }

    /// <summary>
    /// Build a typed function pointer cast expression for a method.
    /// Unlike GetTypedMethodPointerCast, this does NOT check IsAbstract/BasicBlocks.
    /// Used for CoreRuntimeType vtable entries where the extern "C" function exists
    /// in the runtime but the IR method has no compiled body.
    /// </summary>
    private string BuildMethodPointerCast(IRMethod method)
    {
        // Use actual declared parameter types for the cast so it matches the function
        // declaration — needed for overload resolution with MSVC.
        // Only sanitize IL function pointer types (contain parentheses) to void*.
        var retType = SanitizeFuncPtrType(method.ReturnTypeCpp ?? "void");
        var paramTypes = new List<string>();
        // Instance methods have implicit 'this' parameter
        if (!method.IsStatic && method.DeclaringType != null)
        {
            paramTypes.Add($"{method.DeclaringType.CppName}*");
        }
        foreach (var p in method.Parameters)
            paramTypes.Add(SanitizeFuncPtrType(p.CppTypeName));

        var funcPtrType = $"{retType}(*)({string.Join(", ", paramTypes)})";
        return $"(void*)({funcPtrType})&{method.CppName}";
    }

    /// <summary>
    /// Replace IL function pointer types (containing parentheses) with void*.
    /// These types can't be expressed in C++ function pointer casts.
    /// </summary>
    private static string SanitizeFuncPtrType(string cppType)
    {
        if (cppType.Contains("(") || cppType.Contains(")"))
            return "void*";
        return cppType;
    }

    /// <summary>
    /// Replace pointer-to-unknown-struct types with void* for safe function pointer casts.
    /// Types not defined in the compilation unit (BCL types like System_Type*) cause C++ errors.
    /// </summary>
    private string SafeCppType(string cppType)
    {
        if (cppType.EndsWith("*"))
        {
            var baseType = cppType.TrimEnd('*').Trim();
            if (baseType.StartsWith("cil2cpp::") || IsCppPrimitiveType(baseType))
                return cppType;
            // Check if the struct type is defined in the compilation unit
            if (_knownStructTypes == null) BuildKnownStructTypes();
            if (!_knownStructTypes!.Contains(baseType))
                return "void*";
        }
        return cppType;
    }

    private HashSet<string>? _knownStructTypes;
    private void BuildKnownStructTypes()
    {
        _knownStructTypes = new HashSet<string>();
        foreach (var type in _module.Types)
        {
            if (CppNameMapper.IsCompilerGeneratedType(type.ILFullName)) continue;
            if (type.IsInterface && type.Fields.Count == 0) continue;
            _knownStructTypes.Add(type.CppName);
        }
    }

    /// <summary>
    /// Generate unboxing thunks for all value type instance methods that appear in vtable
    /// or interface vtable. When boxed value types are dispatched via vtable, the 'this'
    /// pointer points to the Object header. Thunks adjust 'this' past the header.
    /// </summary>
    private void EmitUnboxThunks(StringBuilder sb, List<IRType> userTypes)
    {
        var generatedMethodTypes = new HashSet<string>();
        foreach (var t in userTypes)
        {
            if (!t.IsInterface && t.Methods.Count > 0)
                generatedMethodTypes.Add(t.CppName);
        }

        var emitted = new HashSet<string>();

        void EmitThunk(IRMethod m)
        {
            if (m.IsAbstract || m.IsStatic) return;
            if (!_declaredFunctionNames.Contains(m.CppName)) return;
            if (_unboxThunkNames.ContainsKey(m.CppName)) return;
            var declType = m.DeclaringType;
            if (declType != null)
            {
                var isDeclCoreRt = declType.IsRuntimeProvided
                    && IRBuilder.CoreRuntimeTypes.Contains(declType.ILFullName);
                if (isDeclCoreRt || !generatedMethodTypes.Contains(declType.CppName))
                    return;
            }
            if (m.BasicBlocks.Count == 0) return;

            var thunkName = $"{m.CppName}__unbox_thunk";
            if (!emitted.Add(thunkName)) return;

            var retType = SanitizeFuncPtrType(m.ReturnTypeCpp ?? "void");
            var paramDecls = new List<string> { "void* __this_boxed" };
            var argNames = new List<string>();
            argNames.Add($"reinterpret_cast<{m.DeclaringType!.CppName}*>(reinterpret_cast<char*>(__this_boxed) + sizeof(cil2cpp::Object))");
            foreach (var p in m.Parameters)
            {
                paramDecls.Add($"{p.CppTypeName} {p.Name}");
                argNames.Add(p.Name);
            }
            var retPrefix = retType == "void" ? "" : "return ";
            sb.AppendLine($"static {retType} {thunkName}({string.Join(", ", paramDecls)}) {{");
            sb.AppendLine($"    {retPrefix}{m.CppName}({string.Join(", ", argNames)});");
            sb.AppendLine("}");

            _unboxThunkNames[m.CppName] = thunkName;
        }

        foreach (var type in userTypes)
        {
            if (!type.IsValueType) continue;

            // VTable methods (ToString, Equals, GetHashCode, etc.)
            foreach (var e in type.VTable)
            {
                if (e.Method != null) EmitThunk(e.Method);
            }

            // Interface methods
            foreach (var impl in type.InterfaceImpls)
            {
                foreach (var m in impl.MethodImpls)
                {
                    if (m != null) EmitThunk(m);
                }
            }
        }
        if (emitted.Count > 0) sb.AppendLine();
    }

    private void EmitVTableData(StringBuilder sb, List<IRType> userTypes)
    {
        // Build set of types whose methods are actually generated as C++ functions.
        // Methods from runtime-provided types (Object, ValueType, Enum, etc.) don't exist
        // as generated functions — VTable entries must use fallbacks for them.
        var generatedMethodTypes = new HashSet<string>();
        foreach (var t in userTypes)
        {
            // Non-core runtime-provided types (Task, Thread) have methods generated from BCL IL
            var isCoreRuntime = t.IsRuntimeProvided && IRBuilder.CoreRuntimeTypes.Contains(t.ILFullName);
            if (!isCoreRuntime && !t.IsDelegate)
                generatedMethodTypes.Add(t.CppName);
        }

        var constructedTypes = _module.ConstructedTypes;

        // Pre-pass: collect extern "C" forward declarations needed for CoreRuntimeType vtable entries.
        // CoreRuntimeType methods (e.g., System_Reflection_MemberInfo_get_Name) are defined in
        // core_methods.cpp but not declared in the generated header. We need forward declarations
        // for them so they can be referenced in vtable arrays.
        var coreRuntimeExternDecls = new HashSet<string>();

        bool any = false;
        foreach (var type in userTypes)
        {
            if (type.IsInterface || type.IsDelegate || type.VTable.Count == 0) continue;
            // Skip VTable for non-constructed types (no instances → no virtual dispatch)
            if (constructedTypes.Count > 0 && !constructedTypes.Contains(type.ILFullName)
                && !_referencedTypeInfoNames.Contains(type.CppName)) continue;

            // Scan vtable entries for CoreRuntimeType methods that need extern "C" decls.
            // This covers both abstract methods (base declarations) and non-abstract bodyless
            // methods (newslot declarations on CoreRuntimeTypes like Type.get_IsClass).
            foreach (var e in type.VTable)
            {
                if (e.Method == null) continue;
                var declType = e.Method.DeclaringType;
                if (declType == null || !IRBuilder.CoreRuntimeTypes.Contains(declType.ILFullName)) continue;
                // Only need extern "C" for methods that aren't already declared
                if (_declaredFunctionNames.Contains(e.Method.CppName)) continue;
                // Only for methods without compiled bodies (runtime-provided implementations)
                if (!e.Method.IsAbstract && e.Method.BasicBlocks.Count > 0) continue;
                // Build extern "C" declaration matching core_methods.cpp signature (void* params)
                var retCpp = e.Method.ReturnTypeCpp ?? "void";
                // Simplify: use void* for all pointer/object params, keep value types
                var paramList = new List<string>();
                if (!e.Method.IsStatic) paramList.Add("void*"); // this
                foreach (var p in e.Method.Parameters)
                {
                    // core_methods.cpp uses void* for all reference types
                    var ptype = p.CppTypeName;
                    if (ptype.EndsWith("*") || ptype == "cil2cpp::Object*" || ptype == "cil2cpp::String*")
                        paramList.Add("void*");
                    else
                        paramList.Add(ptype);
                }
                var retType = retCpp.EndsWith("*") ? "void*" : retCpp;
                var decl = $"extern \"C\" {retType} {e.Method.CppName}({string.Join(", ", paramList)});";
                coreRuntimeExternDecls.Add(decl);
                _declaredFunctionNames.Add(e.Method.CppName);
            }
        }

        // Emit extern "C" forward declarations for CoreRuntimeType methods
        if (coreRuntimeExternDecls.Count > 0)
        {
            sb.AppendLine("// Forward declarations for CoreRuntimeType extern \"C\" methods (defined in core_methods.cpp)");
            foreach (var decl in coreRuntimeExternDecls.OrderBy(d => d))
                sb.AppendLine(decl);
            sb.AppendLine();
        }

        foreach (var type in userTypes)
        {
            if (type.IsInterface || type.IsDelegate || type.VTable.Count == 0) continue;
            // Skip VTable for non-constructed types (no instances → no virtual dispatch)
            if (constructedTypes.Count > 0 && !constructedTypes.Contains(type.ILFullName)
                && !_referencedTypeInfoNames.Contains(type.CppName)) continue;
            if (!any)
            {
                sb.AppendLine("// ===== VTable Data =====");
                any = true;
            }

            // Build lookup of parent vtable entries for inheritance fallback
            var parentVTableEntries = new Dictionary<int, string>();
            {
                var baseType = type.BaseType;
                // Walk up the inheritance chain to find valid vtable entries
                while (baseType != null)
                {
                    for (int si = 0; si < baseType.VTable.Count; si++)
                    {
                        if (parentVTableEntries.ContainsKey(si)) continue;
                        var be = baseType.VTable[si];
                        if (be.Method != null && !be.Method.IsAbstract
                            && _declaredFunctionNames.Contains(be.Method.CppName))
                        {
                            parentVTableEntries[si] = GetTypedMethodPointerCast(be.Method);
                        }
                    }
                    baseType = baseType.BaseType;
                }
            }

            // Method pointer array
            var methods = string.Join(", ", type.VTable.Select((e, idx) =>
            {
                if (e.Method != null && !e.Method.IsAbstract)
                {
                    // If the method's declaring type is a core runtime type or not in generated types,
                    // check if the function is declared (extern "C" from runtime), otherwise use fallback
                    var declType = e.Method.DeclaringType;
                    var isDeclCoreRuntime = declType != null && declType.IsRuntimeProvided
                        && IRBuilder.CoreRuntimeTypes.Contains(declType.ILFullName);
                    if (declType != null && (isDeclCoreRuntime || !generatedMethodTypes.Contains(declType.CppName)))
                    {
                        if (_declaredFunctionNames.Contains(e.Method.CppName))
                        {
                            // Blanket-gated CoreRuntimeType methods have no compiled body
                            // (BasicBlocks.Count == 0) but some are provided as extern "C" in
                            // the runtime (core_methods.cpp). Use BuildMethodPointerCast for those
                            // (doesn't check for a compiled body). Currently only Exception's
                            // virtual methods are implemented — expand as needed.
                            if (isDeclCoreRuntime && e.Method.BasicBlocks.Count == 0
                                && declType.ILFullName == "System.Exception")
                                return BuildMethodPointerCast(e.Method);
                            return GetTypedMethodPointerCast(e.Method);
                        }
                        if (parentVTableEntries.TryGetValue(idx, out var parentFallback))
                            return parentFallback;
                        return ObjectMethodFallbacks.GetValueOrDefault(e.MethodName, "nullptr");
                    }
                    // Check if the function was actually declared in the header
                    if (!_declaredFunctionNames.Contains(e.Method.CppName))
                    {
                        // Fallback: inherit from parent vtable if available
                        if (parentVTableEntries.TryGetValue(idx, out var parentMethod))
                            return parentMethod;
                        return ObjectMethodFallbacks.GetValueOrDefault(e.MethodName, "nullptr");
                    }
                    // Value type vtable methods need unboxing thunks (boxed Object* → value ptr)
                    if (type.IsValueType && _unboxThunkNames.TryGetValue(e.Method.CppName, out var vtThunk))
                        return $"(void*)&{vtThunk}";
                    return GetTypedMethodPointerCast(e.Method);
                }

                // Abstract or bodyless method on CoreRuntimeType — use the extern "C" function
                if (e.Method != null)
                {
                    var declType = e.Method.DeclaringType;
                    if (declType != null && IRBuilder.CoreRuntimeTypes.Contains(declType.ILFullName)
                        && _declaredFunctionNames.Contains(e.Method.CppName))
                    {
                        return BuildMethodPointerCast(e.Method);
                    }
                }

                // Method not assigned — try parent fallback
                if (parentVTableEntries.TryGetValue(idx, out var inherited))
                    return inherited;
                return ObjectMethodFallbacks.GetValueOrDefault(e.MethodName, "nullptr");
            }));
            sb.AppendLine($"static void* {type.CppName}_vtable_methods[] = {{ {methods} }};");
            sb.AppendLine($"static cil2cpp::VTable {type.CppName}_VTable = {{ &{type.CppName}_TypeInfo, {type.CppName}_vtable_methods, {type.VTable.Count} }};");
        }
        if (any) sb.AppendLine();
    }

    private void EmitInterfaceData(StringBuilder sb, List<IRType> userTypes)
    {
        // Build set of types whose methods are generated (same as EmitVTableData)
        var generatedMethodTypes = new HashSet<string>();
        foreach (var t in userTypes)
        {
            var isCoreRuntime = t.IsRuntimeProvided && IRBuilder.CoreRuntimeTypes.Contains(t.ILFullName);
            if (!isCoreRuntime && !t.IsDelegate)
                generatedMethodTypes.Add(t.CppName);
        }

        bool any = false;
        foreach (var type in userTypes)
        {
            var isCoreRuntime = type.IsRuntimeProvided && IRBuilder.CoreRuntimeTypes.Contains(type.ILFullName);
            // Skip CoreRuntimeTypes that don't have vtables (minimal stubs), but allow those
            // with vtables (constructed types that need interface dispatch)
            if (type.IsInterface || (isCoreRuntime && type.VTable.Count == 0) || type.Interfaces.Count == 0) continue;
            if (!any)
            {
                sb.AppendLine("// ===== Interface Data =====");
                any = true;
            }

            // Interfaces pointer array
            var ifaces = string.Join(", ", type.Interfaces.Select(i => $"&{i.CppName}_TypeInfo"));
            sb.AppendLine($"static cil2cpp::TypeInfo* {type.CppName}_interfaces[] = {{ {ifaces} }};");
        }

        // Interface vtable method arrays and InterfaceVTable arrays

        // Pre-pass: generate default-return stubs for interface methods without compiled bodies.
        // Two categories:
        //   1. "Undeclared": method resolved by Pass 5 (non-null m) but body not compiled.
        //      Root cause: ReachabilityAnalyzer doesn't mark interface impl methods as reachable
        //      when the interface is dispatched on. Fix requires RA changes (tracked in plan).
        //   2. "Unresolved": null slot from Pass 5 (method not found in type hierarchy).
        //      Root cause: CoreRuntime reflection types (IReflect, ICustomAttributeProvider) —
        //      architecturally correct since these have runtime-provided implementations.
        var ifaceStubLookup = new Dictionary<string, string>(); // "Type|Iface|slot" → stubFuncName
        var emittedIfaceStubs = new HashSet<string>();
        var undeclaredIfaceMethodCount = 0;
        // Known C++ type names — used to detect open generic params in interface stubs
        var knownCppTypes = new HashSet<string>(_module.Types.Select(t => t.CppName));
        foreach (var prim in new[] { "int8_t", "uint8_t", "int16_t", "uint16_t", "int32_t", "uint32_t",
            "int64_t", "uint64_t", "float", "double", "bool", "char16_t", "intptr_t", "uintptr_t", "void" })
            knownCppTypes.Add(prim);
        foreach (var type in userTypes)
        {
            var isCoreRt2 = type.IsRuntimeProvided && IRBuilder.CoreRuntimeTypes.Contains(type.ILFullName);
            if (type.IsInterface || (isCoreRt2 && type.VTable.Count == 0) || type.InterfaceImpls.Count == 0) continue;
            foreach (var impl in type.InterfaceImpls)
            {
                for (int mi = 0; mi < impl.MethodImpls.Count; mi++)
                {
                    var m = impl.MethodImpls[mi];

                    if (m != null)
                    {
                        // Non-null: method resolved by Pass 5 but body may not be compiled.
                        var declType = m.DeclaringType;
                        var isDeclCoreRt = declType != null && declType.IsRuntimeProvided
                            && IRBuilder.CoreRuntimeTypes.Contains(declType.ILFullName);
                        if (declType != null && (isDeclCoreRt || !generatedMethodTypes.Contains(declType.CppName)))
                            continue; // Core runtime types use nullptr (runtime-handled)
                        if (_declaredFunctionNames.Contains(m.CppName)) continue; // Already declared
                        if (!emittedIfaceStubs.Add(m.CppName)) continue;

                        // Method body not compiled — generate default-return stub.
                        // Root cause: tree-shaking doesn't propagate interface dispatch to
                        // implementing methods. These stubs ensure safe vtable entries until
                        // CompileInterfaceVtableMethodBodies covers all cases.
                        undeclaredIfaceMethodCount++;
                        var sig = m.GetCppSignature();
                        var retDefault = string.IsNullOrEmpty(m.ReturnTypeCpp) || m.ReturnTypeCpp == "void"
                            ? "" : " return {};";
                        sb.AppendLine($"{sig} {{{retDefault} }}");
                        _declaredFunctionNames.Add(m.CppName);
                        _emittedMethodSignatures.Add(sig);
                    }
                    else
                    {
                        // Null: create stub from interface method signature
                        if (mi >= impl.Interface.Methods.Count) continue;
                        var ifaceMethod = impl.Interface.Methods[mi];
                        var stubName = $"{type.CppName}_{impl.Interface.CppName}_{ifaceMethod.Name}";
                        if (_declaredFunctionNames.Contains(stubName)) continue; // Already compiled
                        if (!emittedIfaceStubs.Add(stubName)) continue;

                        var paramList = new List<string> { $"{type.CppName}* __this" };
                        foreach (var p in ifaceMethod.Parameters.Skip(1))
                        {
                            // Replace open generic param types (e.g. IComparer_1_TKey) with void*
                            var pType = p.CppTypeName;
                            var baseType = pType.TrimEnd('*', ' ');
                            if (!string.IsNullOrEmpty(baseType) && !knownCppTypes.Contains(baseType))
                                pType = "void*";
                            paramList.Add($"{pType} {p.Name}");
                        }
                        var retType = string.IsNullOrEmpty(ifaceMethod.ReturnTypeCpp) ? "void" : ifaceMethod.ReturnTypeCpp;
                        // Also sanitize return type for open generic params
                        var retBase = retType.TrimEnd('*', ' ');
                        if (!string.IsNullOrEmpty(retBase) && !knownCppTypes.Contains(retBase))
                            retType = "void*";
                        var retDefault = retType == "void" ? "" : " return {};";

                        sb.AppendLine($"// STUB: unresolved interface method ({impl.Interface.Name}.{ifaceMethod.Name})");
                        sb.AppendLine($"{retType} {stubName}({string.Join(", ", paramList)}) {{{retDefault} }}");

                        ifaceStubLookup[$"{type.CppName}|{impl.Interface.CppName}|{mi}"] = stubName;
                    }
                }
            }
        }
        if (emittedIfaceStubs.Count > 0 || undeclaredIfaceMethodCount > 0)
        {
            if (emittedIfaceStubs.Count > 0) sb.AppendLine();
            Console.Error.WriteLine($"[CIL2CPP] Interface vtable gaps for {_module.Name}: {undeclaredIfaceMethodCount} undeclared (stubs), {ifaceStubLookup.Count} unresolved (stubs)");
        }

        // Unboxing thunks are now generated by EmitUnboxThunks() and stored in _unboxThunkNames.

        var constructedTypesIface = _module.ConstructedTypes;
        foreach (var type in userTypes)
        {
            var isCoreRt = type.IsRuntimeProvided && IRBuilder.CoreRuntimeTypes.Contains(type.ILFullName);
            if (type.IsInterface || (isCoreRt && type.VTable.Count == 0) || type.InterfaceImpls.Count == 0) continue;
            // NOTE: Previously skipped non-constructed types ("no instances → no interface dispatch").
            // WRONG: derived instances use base type interface vtables via type_get_interface_vtable's
            // base_type chain walk. Must emit for ALL types with InterfaceImpls.

            foreach (var impl in type.InterfaceImpls)
            {
                var implRef = impl; // capture for lambda
                var typeRef = type; // capture for lambda
                var methods = string.Join(", ", impl.MethodImpls.Select((m, idx) =>
                {
                    if (m == null)
                    {
                        var key = $"{typeRef.CppName}|{implRef.Interface.CppName}|{idx}";
                        if (ifaceStubLookup.TryGetValue(key, out var stubName))
                            return $"(void*)&{stubName}";
                        // Check if an already-compiled method matches the stub name pattern
                        if (idx < implRef.Interface.Methods.Count)
                        {
                            var ifm = implRef.Interface.Methods[idx];
                            var compiledName = $"{typeRef.CppName}_{implRef.Interface.CppName}_{ifm.Name}";
                            if (_declaredFunctionNames.Contains(compiledName))
                                return $"(void*)&{compiledName}";
                        }
                        return "nullptr";
                    }
                    // If the method's declaring type is a core runtime type or not generated, use nullptr
                    var declType = m.DeclaringType;
                    var isDeclCoreRt = declType != null && declType.IsRuntimeProvided
                        && IRBuilder.CoreRuntimeTypes.Contains(declType.ILFullName);
                    if (declType != null && (isDeclCoreRt || !generatedMethodTypes.Contains(declType.CppName)))
                        return "nullptr";
                    // Check if the function was actually declared in the header
                    if (!_declaredFunctionNames.Contains(m.CppName))
                        return "nullptr";
                    // Value type interface methods need unboxing thunks
                    if (typeRef.IsValueType && !m.IsStatic && _unboxThunkNames.TryGetValue(m.CppName, out var thunkName))
                        return $"(void*)&{thunkName}";
                    // If this method was stubbed by the pre-pass (no basic blocks),
                    // GetTypedMethodPointerCast returns "nullptr". Build cast manually.
                    if (emittedIfaceStubs.Contains(m.CppName))
                    {
                        var retType = SanitizeFuncPtrType(m.ReturnTypeCpp ?? "void");
                        var ptypes = new List<string>();
                        if (!m.IsStatic && m.DeclaringType != null)
                            ptypes.Add($"{m.DeclaringType.CppName}*");
                        foreach (var p in m.Parameters)
                            ptypes.Add(SanitizeFuncPtrType(p.CppTypeName));
                        return $"(void*)({retType}(*)({string.Join(", ", ptypes)}))&{m.CppName}";
                    }
                    return GetTypedMethodPointerCast(m);
                }));
                // MSVC: zero-size arrays are not allowed (C2466)
                if (string.IsNullOrWhiteSpace(methods))
                    methods = "nullptr";
                sb.AppendLine($"static void* {type.CppName}_iface_{impl.Interface.CppName}_methods[] = {{ {methods} }};");
            }

            var entries = type.InterfaceImpls.Select(impl =>
                $"{{ &{impl.Interface.CppName}_TypeInfo, {type.CppName}_iface_{impl.Interface.CppName}_methods, {impl.MethodImpls.Count} }}");
            sb.AppendLine($"static cil2cpp::InterfaceVTable {type.CppName}_interface_vtables[] = {{");
            sb.AppendLine($"    {string.Join(",\n    ", entries)}");
            sb.AppendLine("};");
        }
        if (any) sb.AppendLine();
    }

    private void EmitGenericVarianceData(StringBuilder sb, List<IRType> userTypes)
    {
        var typeInfoLookup = BuildTypeInfoExprLookup();
        var constructedTypes = _module.ConstructedTypes;
        bool any = false;
        foreach (var type in userTypes)
        {
            if (type.IsRuntimeProvided) continue;
            if (!type.IsGenericInstance || type.GenericArguments.Count == 0
                || type.GenericParameterVariances.Count == 0) continue;

            // Skip types where all generic parameters are invariant — no variance checks needed
            if (type.GenericParameterVariances.All(v => v == 0)) continue;

            // All generic types with non-trivial variance need their data emitted,
            // even Tier 2 types — runtime variance checks (type_is_variant_assignable)
            // access generic_arguments/generic_variances on interface vtable entries.

            // Verify all generic arguments have TypeInfo available
            bool allArgsResolvable = type.GenericArguments.All(arg =>
                typeInfoLookup.ContainsKey(arg));
            if (!allArgsResolvable) continue;

            if (!any)
            {
                sb.AppendLine("// ===== Generic Variance Data =====");
                any = true;
            }

            // Emit TypeInfo pointer array for generic arguments
            var argRefs = type.GenericArguments.Select(arg => typeInfoLookup[arg]);
            sb.AppendLine($"static cil2cpp::TypeInfo* {type.CppName}_generic_args[] = {{ {string.Join(", ", argRefs)} }};");

            // Emit variance array
            var variances = type.GenericParameterVariances.Select(v => ((byte)v).ToString());
            sb.AppendLine($"static uint8_t {type.CppName}_generic_variances[] = {{ {string.Join(", ", variances)} }};");
        }
        if (any) sb.AppendLine();
    }

    private void EmitFinalizerWrappers(StringBuilder sb, List<IRType> userTypes)
    {
        bool any = false;
        foreach (var type in userTypes)
        {
            if (type.Finalizer == null) continue;
            // Skip runtime-provided types — their Finalize method isn't generated
            if (type.IsRuntimeProvided) continue;
            // BCL types have stub finalizers — use System_Object_Finalize as fallback
            var isStubbed = type.SourceKind == AssemblyKind.BCL;
            if (!any)
            {
                sb.AppendLine("// ===== Finalizer Wrappers =====");
                any = true;
            }

            sb.AppendLine($"static void {type.CppName}_finalizer_wrapper(cil2cpp::Object* obj) {{");
            if (isStubbed)
                sb.AppendLine($"    System_Object_Finalize((System_Object*)obj);");
            else
                sb.AppendLine($"    {type.Finalizer.CppName}(({type.CppName}*)obj);");
            sb.AppendLine("}");
        }
        if (any) sb.AppendLine();
    }

    /// <summary>
    /// Build a lookup table mapping IL type full names to TypeInfo pointer expressions.
    /// Only includes types with TypeInfo emitted in generated code (non-runtime-provided user types + primitives).
    /// </summary>
    /// <summary>
    /// Maps an IL full name to its ECMA-335 CorElementType code (II.23.1.16).
    /// </summary>
    /// <summary>
    /// Maps an IL full name to its ECMA-335 CorElementType code (II.23.1.16).
    /// Returns 0x00 if the name is not a well-known primitive/built-in type.
    /// </summary>
    private static byte GetCorElementTypeForPrimitive(string ilFullName)
    {
        return ilFullName switch
        {
            "System.Void"    => 0x01, // ELEMENT_TYPE_VOID
            "System.Boolean" => 0x02, // ELEMENT_TYPE_BOOLEAN
            "System.Char"    => 0x03, // ELEMENT_TYPE_CHAR
            "System.SByte"   => 0x04, // ELEMENT_TYPE_I1
            "System.Byte"    => 0x05, // ELEMENT_TYPE_U1
            "System.Int16"   => 0x06, // ELEMENT_TYPE_I2
            "System.UInt16"  => 0x07, // ELEMENT_TYPE_U2
            "System.Int32"   => 0x08, // ELEMENT_TYPE_I4
            "System.UInt32"  => 0x09, // ELEMENT_TYPE_U4
            "System.Int64"   => 0x0A, // ELEMENT_TYPE_I8
            "System.UInt64"  => 0x0B, // ELEMENT_TYPE_U8
            "System.Single"  => 0x0C, // ELEMENT_TYPE_R4
            "System.Double"  => 0x0D, // ELEMENT_TYPE_R8
            "System.String"  => 0x0E, // ELEMENT_TYPE_STRING
            "System.IntPtr"  => 0x18, // ELEMENT_TYPE_I
            "System.UIntPtr" => 0x19, // ELEMENT_TYPE_U
            "System.Object"  => 0x1C, // ELEMENT_TYPE_OBJECT
            _ => 0x00,
        };
    }

    /// <summary>
    /// Maps an IRType to its ECMA-335 CorElementType code (II.23.1.16).
    /// Delegates to GetCorElementTypeForPrimitive for well-known types,
    /// then applies structural checks for arrays, generics, enums, value types.
    /// </summary>
    private static byte GetCorElementType(IRType type)
    {
        // Check well-known primitive/built-in types first
        var code = GetCorElementTypeForPrimitive(type.ILFullName);
        if (code != 0x00) return code;

        // Structural type checks per ECMA-335 II.23.1.16
        if (type.ILFullName.EndsWith("[]")) return 0x1D;  // ELEMENT_TYPE_SZARRAY
        if (type.IsGenericInstance) return 0x15;           // ELEMENT_TYPE_GENERICINST
        return type.IsValueType ? (byte)0x11 : (byte)0x12; // VALUETYPE or CLASS
    }

    private Dictionary<string, string>? _typeInfoExprLookupCache;

    private Dictionary<string, string> BuildTypeInfoExprLookup()
    {
        if (_typeInfoExprLookupCache != null)
            return _typeInfoExprLookupCache;

        var lookup = new Dictionary<string, string>();

        // All types (including runtime-provided) — their TypeInfo symbols exist in generated code
        // Runtime-provided types need to be in the lookup so that generic variance data
        // (e.g., IEnumerable<MemberInfo>) can reference their TypeInfo pointers.
        foreach (var type in _module.Types)
        {
            if (!CppNameMapper.IsCompilerGeneratedType(type.ILFullName))
            {
                lookup[type.ILFullName] = $"&{type.CppName}_TypeInfo";
            }
        }

        // Primitive types (array element types)
        foreach (var entry in _module.PrimitiveTypeInfos.Values)
        {
            lookup[entry.ILFullName] = $"&{entry.CppMangledName}_TypeInfo";
        }

        _typeInfoExprLookupCache = lookup;
        return lookup;
    }

    private void EmitReflectionMetadata(StringBuilder sb, List<IRType> userTypes)
    {
        var typeInfoLookup = BuildTypeInfoExprLookup();
        bool any = false;
        var emittedReflection = new HashSet<string>();
        var reflectionTargets = _module.ReflectionTargetTypes;

        foreach (var type in userTypes)
        {
            if (type.IsRuntimeProvided || type.IsDelegate) continue;
            if (CppNameMapper.IsRuntimeExceptionType(type.ILFullName)) continue;
            // Deduplicate — BCL proxies may appear multiple times
            if (!emittedReflection.Add(type.CppName)) continue;

            // Field/method metadata: gated by reflection targets + enum types
            var skipFieldMethod = !type.IsEnum
                && reflectionTargets.Count > 0 && !reflectionTargets.Contains(type.ILFullName);
            // Property metadata: always emitted for types with properties
            var reflectableProperties = type.Properties;

            var allFields = skipFieldMethod ? new List<IRField>()
                : type.Fields.Concat(type.StaticFields).ToList();
            var reflectableMethods = skipFieldMethod ? new List<IRMethod>()
                : type.Methods.Where(m => !CppNameMapper.IsCompilerGeneratedType(m.Name)).ToList();

            if (allFields.Count == 0 && reflectableMethods.Count == 0 && reflectableProperties.Count == 0) continue;

            if (!any)
            {
                sb.AppendLine("// ===== Reflection Metadata =====");
                any = true;
            }

            // Emit custom attribute arrays for type and fields
            // Type-level attributes
            if (type.CustomAttributes.Count > 0)
                EmitAttributeInfoArray(sb, $"{type.CppName}_custom_attrs", type.CustomAttributes);

            // Field-level attributes
            foreach (var field in allFields)
            {
                if (field.CustomAttributes.Count > 0)
                    EmitAttributeInfoArray(sb, $"{type.CppName}_{field.CppName}_attrs", field.CustomAttributes);
            }

            // Method-level attributes — use index suffix to disambiguate overloaded methods
            for (int mi = 0; mi < reflectableMethods.Count; mi++)
            {
                var method = reflectableMethods[mi];
                if (method.CustomAttributes.Count > 0)
                    EmitAttributeInfoArray(sb, $"{type.CppName}_m{mi}_attrs", method.CustomAttributes);
            }

            // Emit FieldInfo array
            if (allFields.Count > 0)
            {
                sb.AppendLine($"static cil2cpp::FieldInfo {type.CppName}_fields[] = {{");
                foreach (var field in allFields)
                {
                    var fieldTypeExpr = typeInfoLookup.GetValueOrDefault(field.FieldTypeName, "nullptr");
                    // Primitive types (System_Byte = uint8_t, etc.) don't have struct layout;
                    // their m_value field is at offset 0 by definition.
                    var offsetExpr = field.IsStatic || type.IsPrimitiveType
                        ? "0"
                        : $"offsetof({type.CppName}, {field.CppName})";
                    var fieldAttrsExpr = field.CustomAttributes.Count > 0
                        ? $"{type.CppName}_{field.CppName}_attrs" : "nullptr";
                    var constantExpr = "0";
                    if (field.ConstantValue != null && field.ConstantValue is not string)
                    {
                        try { constantExpr = FormatEnumInt64(EnumConstantToInt64(field.ConstantValue)); }
                        catch { /* Non-numeric constant (e.g. string) — leave as 0 */ }
                    }
                    sb.AppendLine($"    {{ .name = \"{field.Name}\", " +
                        $".declaring_type = &{type.CppName}_TypeInfo, " +
                        $".field_type = {fieldTypeExpr}, " +
                        $".offset = {offsetExpr}, " +
                        $".flags = 0x{field.Attributes:X4}, " +
                        $".custom_attributes = {fieldAttrsExpr}, " +
                        $".custom_attribute_count = {field.CustomAttributes.Count}, " +
                        $".constant_value = {constantExpr} }},");
                }
                sb.AppendLine("};");
            }

            // Emit MethodInfo parameter type arrays and MethodInfo array
            if (reflectableMethods.Count > 0)
            {
                // First: parameter type arrays for methods that have parameters
                for (int mi = 0; mi < reflectableMethods.Count; mi++)
                {
                    var method = reflectableMethods[mi];
                    if (method.Parameters.Count == 0) continue;
                    var paramTypeExprs = method.Parameters
                        .Select(p => typeInfoLookup.GetValueOrDefault(p.ILTypeName, "nullptr"))
                        .ToList();
                    sb.AppendLine($"static cil2cpp::TypeInfo* {type.CppName}_m{mi}_param_types[] = {{ {string.Join(", ", paramTypeExprs)} }};");
                }

                // Second: MethodInfo array
                sb.AppendLine($"static cil2cpp::MethodInfo {type.CppName}_methods[] = {{");
                for (int mi = 0; mi < reflectableMethods.Count; mi++)
                {
                    var method = reflectableMethods[mi];
                    var returnTypeExpr = typeInfoLookup.GetValueOrDefault(
                        method.ReturnType?.ILFullName ?? "", "nullptr");
                    var paramTypesExpr = method.Parameters.Count > 0
                        ? $"{type.CppName}_m{mi}_param_types" : "nullptr";
                    // Check if the method was actually declared (has valid signature)
                    var hasValidSignature = !method.IsAbstract && !method.IsInternalCall
                        && method.BasicBlocks.Count > 0
                        && !method.Parameters.Any(p => p.CppTypeName.Contains("(") || p.CppTypeName.Contains(")"))
                        && !(method.ReturnTypeCpp?.Contains("(") == true)
                        && _declaredFunctionNames.Contains(method.CppName);
                    var methodPtrExpr = hasValidSignature
                        ? GetTypedMethodPointerCast(method)
                        : "nullptr";
                    var methodAttrsExpr = method.CustomAttributes.Count > 0
                        ? $"{type.CppName}_m{mi}_attrs" : "nullptr";

                    sb.AppendLine($"    {{ .name = \"{method.Name}\", " +
                        $".declaring_type = &{type.CppName}_TypeInfo, " +
                        $".return_type = {returnTypeExpr}, " +
                        $".parameter_types = {paramTypesExpr}, " +
                        $".parameter_count = {method.Parameters.Count}, " +
                        $".method_pointer = {methodPtrExpr}, " +
                        $".flags = 0x{method.Attributes:X4}, " +
                        $".vtable_slot = {method.VTableSlot}, " +
                        $".custom_attributes = {methodAttrsExpr}, " +
                        $".custom_attribute_count = {method.CustomAttributes.Count} }},");
                }
                sb.AppendLine("};");
            }

            // Emit PropertyInfo array
            if (reflectableProperties.Count > 0)
            {
                sb.AppendLine($"static cil2cpp::PropertyInfo {type.CppName}_properties[] = {{");
                foreach (var prop in reflectableProperties)
                {
                    var propTypeExpr = typeInfoLookup.GetValueOrDefault(prop.PropertyTypeName, "nullptr");
                    var getterExpr = "nullptr";
                    var setterExpr = "nullptr";
                    if (prop.Getter != null && _declaredFunctionNames.Contains(prop.Getter.CppName))
                        getterExpr = $"(void*){prop.Getter.CppName}";
                    if (prop.Setter != null && _declaredFunctionNames.Contains(prop.Setter.CppName))
                        setterExpr = $"(void*){prop.Setter.CppName}";
                    sb.AppendLine($"    {{ .name = \"{prop.Name}\", " +
                        $".declaring_type = &{type.CppName}_TypeInfo, " +
                        $".property_type = {propTypeExpr}, " +
                        $".getter = {getterExpr}, " +
                        $".setter = {setterExpr}, " +
                        $".flags = 0x{prop.Attributes:X4} }},");
                }
                sb.AppendLine("};");
            }
        }
        if (any) sb.AppendLine();
    }

    /// <summary>
    /// Emit a single CustomAttributeInfo array and its argument arrays.
    /// </summary>
    private void EmitAttributeInfoArray(StringBuilder sb, string arrayName, List<IRCustomAttribute> attrs)
    {
        // First, emit argument arrays (including nested array element arrays)
        for (int i = 0; i < attrs.Count; i++)
        {
            var attr = attrs[i];
            if (attr.ConstructorArgs.Count > 0)
            {
                // Emit nested array element arrays first
                for (int j = 0; j < attr.ConstructorArgs.Count; j++)
                {
                    var arg = attr.ConstructorArgs[j];
                    if (arg.Kind == AttributeArgKind.Array && arg.ArrayElements is { Count: > 0 })
                    {
                        EmitAttributeArgArray(sb, $"{arrayName}_{i}_a{j}_elems", arg.ArrayElements);
                    }
                }

                sb.AppendLine($"static cil2cpp::CustomAttributeArg {arrayName}_{i}_args[] = {{");
                for (int j = 0; j < attr.ConstructorArgs.Count; j++)
                {
                    var arg = attr.ConstructorArgs[j];
                    var valueInit = FormatAttributeArgValue(arg);
                    var arrayInit = FormatAttributeArgArrayFields(arg, $"{arrayName}_{i}_a{j}_elems");
                    sb.AppendLine($"    {{ .type_name = \"{EscapeCppString(arg.TypeName)}\", {valueInit}{arrayInit} }},");
                }
                sb.AppendLine("};");
            }
        }

        // Then emit the CustomAttributeInfo array
        sb.AppendLine($"static cil2cpp::CustomAttributeInfo {arrayName}[] = {{");
        for (int i = 0; i < attrs.Count; i++)
        {
            var attr = attrs[i];
            var argsExpr = attr.ConstructorArgs.Count > 0
                ? $"{arrayName}_{i}_args" : "nullptr";
            sb.AppendLine($"    {{ .attribute_type_name = \"{attr.AttributeTypeName}\", " +
                $".args = {argsExpr}, " +
                $".arg_count = {attr.ConstructorArgs.Count} }},");
        }
        sb.AppendLine("};");
    }

    /// <summary>
    /// Emit a static array of CustomAttributeArg for array-typed attribute arguments.
    /// </summary>
    private void EmitAttributeArgArray(StringBuilder sb, string varName, List<IRAttributeArg> elements)
    {
        sb.AppendLine($"static cil2cpp::CustomAttributeArg {varName}[] = {{");
        foreach (var elem in elements)
        {
            var valueInit = FormatAttributeArgValue(elem);
            sb.AppendLine($"    {{ .type_name = \"{EscapeCppString(elem.TypeName)}\", {valueInit}, .array_elements = nullptr, .array_count = 0 }},");
        }
        sb.AppendLine("};");
    }

    /// <summary>
    /// Format the array_elements and array_count fields for a CustomAttributeArg initializer.
    /// </summary>
    private static string FormatAttributeArgArrayFields(IRAttributeArg arg, string elemVarName)
    {
        if (arg.Kind == AttributeArgKind.Array && arg.ArrayElements is { Count: > 0 })
            return $", .array_elements = {elemVarName}, .array_count = {arg.ArrayElements.Count}";
        return ", .array_elements = nullptr, .array_count = 0";
    }

    /// <summary>
    /// Format a custom attribute argument value for C++ designated initializer.
    /// </summary>
    private static string FormatAttributeArgValue(IRAttributeArg arg)
    {
        if (arg.Value == null && arg.Kind != AttributeArgKind.Array)
            return ".string_val = nullptr";

        return arg.Kind switch
        {
            AttributeArgKind.String => $".string_val = \"{EscapeCppString(arg.Value?.ToString() ?? "")}\"",
            AttributeArgKind.Type => $".string_val = \"{EscapeCppString(arg.Value?.ToString() ?? "")}\"",
            AttributeArgKind.Float when arg.TypeName == "System.Single" => $".float_val = {FormatFloat(arg.Value!)}",
            AttributeArgKind.Float => $".float_val = {FormatDouble(arg.Value!)}",
            AttributeArgKind.Enum => $".int_val = {EnumConstantToInt64(arg.Value!)}",
            AttributeArgKind.Array => ".int_val = 0",  // value union unused for arrays
            AttributeArgKind.Int when arg.TypeName == "System.Boolean" => $".int_val = {((bool)arg.Value! ? 1 : 0)}",
            AttributeArgKind.Int when arg.TypeName == "System.Char" => $".int_val = {(int)(char)arg.Value!}",
            _ => $".int_val = {EnumConstantToInt64(arg.Value!)}",
        };
    }

    private static string EscapeCppString(string s) =>
        s.Replace("\\", "\\\\").Replace("\"", "\\\"").Replace("\n", "\\n").Replace("\r", "\\r");

    private static string FormatFloat(object val) =>
        ((float)val).ToString("G9", System.Globalization.CultureInfo.InvariantCulture);

    private static string FormatDouble(object val) =>
        ((double)val).ToString("G17", System.Globalization.CultureInfo.InvariantCulture);

    /// <summary>
    /// Convert an enum constant value to Int64, handling UInt64 values that exceed Int64.MaxValue
    /// via unchecked two's-complement cast (bit-pattern preserving).
    /// </summary>
    private static long EnumConstantToInt64(object value) =>
        value is ulong u ? unchecked((long)u) : Convert.ToInt64(value);

    /// <summary>
    /// Format an int64 enum value as a C++ literal.
    /// Int64.MinValue (-2^63) can't be written as -9223372036854775808LL because the
    /// unsigned magnitude exceeds int64 max, triggering MSVC C4838 narrowing warning.
    /// Use hex format for negative values to avoid this.
    /// </summary>
    private static string FormatEnumInt64(long value)
    {
        if (value >= 0)
            return $"{value}LL";
        // Negative values: emit as (int64_t)0xHEXULL to avoid narrowing
        return $"(int64_t)0x{unchecked((ulong)value):X}ULL";
    }

    /// <summary>
    /// Emit extern "C" declarations and managed wrapper functions for P/Invoke methods.
    /// </summary>
    // C stdlib functions that are already declared via standard headers — don't re-declare
    private static readonly HashSet<string> CStdlibFunctions = new()
    {
        "strlen", "strcpy", "strncpy", "strcat", "strncat", "strcmp", "strncmp",
        "memcpy", "memmove", "memset", "memcmp", "malloc", "calloc", "realloc", "free",
        "printf", "sprintf", "snprintf", "fprintf", "puts", "putchar", "getchar",
        "abs", "atoi", "atof", "atol", "rand", "srand", "exit", "abort",
        "qsort", "bsearch", "time", "clock", "sin", "cos", "tan", "sqrt", "pow",
        "fopen", "fclose", "fread", "fwrite", "fseek", "ftell"
    };

    // .NET internal P/Invoke modules — no extern declarations needed
    // (QCall = CLR-internal; libSystem.* = Unix-only native shims; ucrtbase = C runtime)
    private static readonly HashSet<string> InternalPInvokeModules = new(StringComparer.OrdinalIgnoreCase)
    {
        "QCall", "QCall.dll", "libSystem.Native", "libSystem.Globalization.Native",
        "System.Globalization.Native", "System.Native", "System.IO.Compression.Native",
        "System.Security.Cryptography.Native.OpenSsl", "System.Net.Security.Native",
        "ucrtbase", "ucrtbase.dll"
    };

    private void EmitPInvokeDeclarations(StringBuilder sb, List<IRType> userTypes,
        HashSet<string> emittedMethodSignatures)
    {
        // Scan ALL module types (not just deduplicated userTypes) — partial classes like
        // Interop.Kernel32 span multiple assemblies, and deduplication drops P/Invoke methods
        var allPInvokeMethods = _module.Types
            .Where(t => !t.IsInterface && !t.IsDelegate && !t.IsRuntimeProvided)
            .SelectMany(t => t.Methods)
            .Where(m => m.IsPInvoke && !string.IsNullOrEmpty(m.PInvokeModule))
            .ToList();

        if (allPInvokeMethods.Count == 0) return;

        // Exclude InternalPInvokeModules from extern declarations (they're available via system headers)
        // but keep them for wrapper generation (the managed→native wrapper functions are still needed)
        var pinvokeMethods = allPInvokeMethods
            .Where(m => !InternalPInvokeModules.Contains(m.PInvokeModule!))
            .ToList();

        // Filter out C stdlib functions and methods with delegate/function pointer parameters
        // (our codegen doesn't produce valid C function pointer types for extern "C" declarations)
        var needsExternDecl = pinvokeMethods
            .Where(m => !CStdlibFunctions.Contains(m.PInvokeEntryPoint ?? m.Name))
            .Where(m => !m.Parameters.Any(p =>
                IsDelegateType(p.ParameterType) || p.CppTypeName.StartsWith("method")))
            .ToList();

        // Track which entry points have valid extern "C" declarations
        // C stdlib functions are always available via standard headers
        var declaredEntryPoints = new HashSet<string>(CStdlibFunctions);
        // Track the declared parameter types for each entry point (for wrapper compatibility check)
        var declaredParamTypes = new Dictionary<string, List<string>>();
        if (needsExternDecl.Count > 0)
        {
            sb.AppendLine("// ===== P/Invoke Declarations =====");
            sb.AppendLine("extern \"C\" {");

            // Deduplicate extern declarations by entry point name only
            // (C doesn't support function overloading — same entry point = same function)
            // Multiple __PInvoke methods may share an entry point but have different param types
            // (e.g., ReadFile with int32_t* vs NativeOverlapped*). We pick the best candidate:
            // prefer methods with only C-primitive params (no struct pointers like NativeOverlapped*).
            var bestExternCandidate = new Dictionary<string, (IRMethod method, int score)>();
            foreach (var method in needsExternDecl)
            {
                var entryPoint = method.PInvokeEntryPoint ?? method.Name;
                var charSet = method.PInvokeCharSet;
                var retType = GetPInvokeNativeType(method.ReturnTypeCpp, null, charSet, method.ReturnMarshalAs);
                if (!IsValidExternCType(retType)) continue;

                bool hasInvalidType = false;
                int structPointerCount = 0;
                foreach (var p in method.Parameters)
                {
                    var nt = GetPInvokeNativeType(p.CppTypeName, p.ParameterType, charSet, p.MarshalAs);
                    if (!IsValidExternCType(nt)) { hasInvalidType = true; break; }
                    // Count struct pointers (pointer to non-primitive type)
                    if (nt.EndsWith("*"))
                    {
                        var baseType = nt.TrimEnd('*').Trim();
                        if (!IsCppPrimitiveType(baseType) && baseType != "void")
                            structPointerCount++;
                    }
                }
                if (hasInvalidType) continue;

                // Lower score = better candidate (fewer struct pointers)
                if (!bestExternCandidate.TryGetValue(entryPoint, out var existing) ||
                    structPointerCount < existing.score)
                {
                    bestExternCandidate[entryPoint] = (method, structPointerCount);
                }
            }

            // Emit extern declarations from the best candidates
            foreach (var (entryPoint, (method, _)) in bestExternCandidate.OrderBy(kv => kv.Key))
            {
                var cs = method.PInvokeCharSet;
                var retType = GetPInvokeNativeType(method.ReturnTypeCpp, null, cs, method.ReturnMarshalAs);
                var nativeParamTypes = new List<string>();
                foreach (var p in method.Parameters)
                    nativeParamTypes.Add(GetPInvokeNativeType(p.CppTypeName, p.ParameterType, cs, p.MarshalAs));

                var paramParts = new List<string>();
                for (int i = 0; i < method.Parameters.Count; i++)
                    paramParts.Add($"{nativeParamTypes[i]} p{i}");
                var paramDecl = string.Join(", ", paramParts);
                // ECMA-335 II.15.5.1 — Emit calling convention for x86 correctness.
                // On x64, MSVC silently ignores __stdcall/__cdecl/__fastcall.
                var callingConv = method.PInvokeCallingConvention switch
                {
                    PInvokeCallingConvention.StdCall  => "__stdcall ",
                    PInvokeCallingConvention.FastCall => "__fastcall ",
                    PInvokeCallingConvention.ThisCall => "__thiscall ",
                    _ => "" // Cdecl is the default for extern "C", no annotation needed
                };
                sb.AppendLine($"    {retType} {callingConv}{entryPoint}({paramDecl});");
                declaredEntryPoints.Add(entryPoint);
                declaredParamTypes[entryPoint] = nativeParamTypes;
            }

            sb.AppendLine("}");
            sb.AppendLine();
        }

        // Generate managed wrappers (deduplicate by C++ signature)
        // Only generate wrappers for entry points that have a valid extern "C" declaration
        // AND whose parameter types match the declared extern (to avoid type mismatches).
        // Multiple __PInvoke inner methods with the same entry point and compatible types
        // each get their own wrapper (they may have different C++ names).
        sb.AppendLine("// ===== P/Invoke Wrappers =====");
        foreach (var method in allPInvokeMethods)
        {
            var entryPoint = method.PInvokeEntryPoint ?? method.Name;
            if (!declaredEntryPoints.Contains(entryPoint)) continue;
            var sig = method.GetCppSignature();
            if (!emittedMethodSignatures.Add(sig)) continue; // skip exact duplicates

            // Check parameter type compatibility with the declared extern
            var wrapperCharSet = method.PInvokeCharSet;
            declaredParamTypes.TryGetValue(entryPoint, out var externParamTypes);
            if (externParamTypes != null)
            {
                if (method.Parameters.Count != externParamTypes.Count) continue;
                bool compatible = true;
                for (int i = 0; i < method.Parameters.Count; i++)
                {
                    var methodType = GetPInvokeNativeType(method.Parameters[i].CppTypeName,
                        method.Parameters[i].ParameterType, wrapperCharSet, method.Parameters[i].MarshalAs);
                    if (methodType != externParamTypes[i])
                    {
                        // Allow any pointer ↔ pointer mismatch — same DLL entry point,
                        // just different managed overloads (e.g., setsockopt with int32_t* vs Linger*)
                        if (methodType.EndsWith("*") && externParamTypes[i].EndsWith("*"))
                            continue;
                        // Allow integer width/signedness mismatch (e.g., uint32_t vs int32_t
                        // for WSASocketW's group/flags params) — same DLL entry, trivially castable
                        if (IsCppPrimitiveType(methodType) && IsCppPrimitiveType(externParamTypes[i]))
                            continue;
                        compatible = false; break;
                    }
                }
                if (!compatible) continue;
            }
            var retType = method.ReturnTypeCpp;

            sb.AppendLine($"// P/Invoke: {method.PInvokeModule}!{entryPoint}");
            sb.AppendLine($"{sig} {{");

            // Marshal arguments per ECMA-335 II.15.5.4
            var callArgs = new List<string>();
            for (int i = 0; i < method.Parameters.Count; i++)
            {
                var param = method.Parameters[i];
                if (IsStringType(param.CppTypeName))
                {
                    // C.7: [MarshalAs] on parameter overrides method-level CharSet
                    bool useUnicode;
                    if (param.MarshalAs == IR.MarshalAsType.LPWStr)
                        useUnicode = true;
                    else if (param.MarshalAs == IR.MarshalAsType.LPStr
                             || param.MarshalAs == IR.MarshalAsType.LPUtf8Str)
                        useUnicode = false;
                    else
                        useUnicode = wrapperCharSet == IR.PInvokeCharSet.Unicode
                                     || wrapperCharSet == IR.PInvokeCharSet.Auto;

                    if (useUnicode)
                    {
                        // Unicode: pass UTF-16 pointer directly (zero-copy)
                        sb.AppendLine($"    auto __p{i} = {param.CppName} ? cil2cpp::string_get_raw_data({param.CppName}) : (const char16_t*)nullptr;");
                    }
                    else
                    {
                        // Ansi/UTF-8: convert to narrow string
                        sb.AppendLine($"    auto __p{i} = cil2cpp::string_to_utf8({param.CppName});");
                    }
                    callArgs.Add($"__p{i}");
                }
                else if (IsDelegateType(param.ParameterType))
                {
                    var fnPtrType = GetDelegateFunctionPointerType(param.ParameterType);
                    sb.AppendLine($"    auto __p{i} = reinterpret_cast<{fnPtrType}>(reinterpret_cast<cil2cpp::Delegate*>({param.CppName})->method_ptr);");
                    callArgs.Add($"__p{i}");
                }
                else if (param.CppTypeName is "intptr_t" or "uintptr_t")
                {
                    // ECMA-335: native int → target type for native calls
                    // Cast to the extern's parameter type (may be void*, int32_t*, etc.)
                    if (externParamTypes != null && i < externParamTypes.Count)
                    {
                        var targetType = externParamTypes[i];
                        sb.AppendLine($"    auto __p{i} = reinterpret_cast<{targetType}>({param.CppName});");
                        callArgs.Add($"__p{i}");
                    }
                    else
                    {
                        // No extern declaration — pass through as-is (uintptr_t → size_t is implicit)
                        callArgs.Add(param.CppName);
                    }
                }
                else if (param.CppTypeName.EndsWith("*") && externParamTypes != null
                         && i < externParamTypes.Count && externParamTypes[i].EndsWith("*")
                         && param.CppTypeName != externParamTypes[i])
                {
                    // Pointer type mismatch (e.g., Linger* vs int32_t* for setsockopt overloads)
                    // Cast through void* to match the extern "C" declaration
                    sb.AppendLine($"    auto __p{i} = reinterpret_cast<{externParamTypes[i]}>({param.CppName});");
                    callArgs.Add($"__p{i}");
                }
                else if (param.CppTypeName == "bool")
                {
                    // ECMA-335: Boolean → Win32 BOOL (int32_t)
                    sb.AppendLine($"    auto __p{i} = static_cast<int32_t>({param.CppName});");
                    callArgs.Add($"__p{i}");
                }
                else if (param.MarshalAs == IR.MarshalAsType.LPArray
                         && param.CppTypeName.Contains("Array"))
                {
                    // C.7.3: [MarshalAs(UnmanagedType.LPArray)] — pass raw data pointer from managed array
                    // cil2cpp::array_data() returns void* to the contiguous element buffer
                    sb.AppendLine($"    auto __p{i} = {param.CppName} ? cil2cpp::array_data(reinterpret_cast<cil2cpp::Array*>({param.CppName})) : nullptr;");
                    callArgs.Add($"__p{i}");
                }
                else if (externParamTypes != null && i < externParamTypes.Count
                         && param.CppTypeName != externParamTypes[i]
                         && IsCppPrimitiveType(GetPInvokeNativeType(param.CppTypeName, param.ParameterType, wrapperCharSet, param.MarshalAs))
                         && IsCppPrimitiveType(externParamTypes[i]))
                {
                    // Integer type mismatch (e.g., uint32_t vs int32_t for WSASocketW overloads)
                    sb.AppendLine($"    auto __p{i} = static_cast<{externParamTypes[i]}>({param.CppName});");
                    callArgs.Add($"__p{i}");
                }
                else
                {
                    callArgs.Add(param.CppName);
                }
            }

            var argStr = string.Join(", ", callArgs);

            // SetLastError support (ECMA-335 II.15.5)
            if (method.PInvokeSetLastError)
                sb.AppendLine("    cil2cpp::set_last_pinvoke_error(0);");

            if (retType == "void")
            {
                sb.AppendLine($"    {entryPoint}({argStr});");
                if (method.PInvokeSetLastError)
                    sb.AppendLine("    cil2cpp::capture_last_pinvoke_error();");
            }
            else if (IsStringType(retType))
            {
                sb.AppendLine($"    auto __ret = {entryPoint}({argStr});");
                if (method.PInvokeSetLastError)
                    sb.AppendLine("    cil2cpp::capture_last_pinvoke_error();");
                if (wrapperCharSet == IR.PInvokeCharSet.Unicode
                    || wrapperCharSet == IR.PInvokeCharSet.Auto)
                {
                    // Unicode return: null-terminated wchar_t*/char16_t* → managed String
                    sb.AppendLine("    if (!__ret) return nullptr;");
                    sb.AppendLine("    int32_t __retlen = 0;");
                    sb.AppendLine("    while (reinterpret_cast<const char16_t*>(__ret)[__retlen]) ++__retlen;");
                    sb.AppendLine("    return cil2cpp::string_create_utf16(reinterpret_cast<const cil2cpp::Char*>(__ret), __retlen);");
                }
                else
                {
                    sb.AppendLine("    return cil2cpp::string_literal(__ret);");
                }
            }
            else if (retType is "intptr_t" or "uintptr_t")
            {
                // void* from native → intptr_t/uintptr_t in managed
                sb.AppendLine($"    auto __ret = {entryPoint}({argStr});");
                if (method.PInvokeSetLastError)
                    sb.AppendLine("    cil2cpp::capture_last_pinvoke_error();");
                sb.AppendLine($"    return reinterpret_cast<{retType}>(__ret);");
            }
            else if (retType == "bool")
            {
                // Win32 BOOL (int32_t) → C++ bool
                sb.AppendLine($"    auto __ret = {entryPoint}({argStr});");
                if (method.PInvokeSetLastError)
                    sb.AppendLine("    cil2cpp::capture_last_pinvoke_error();");
                sb.AppendLine("    return __ret != 0;");
            }
            else
            {
                if (method.PInvokeSetLastError)
                {
                    sb.AppendLine($"    auto __ret = {entryPoint}({argStr});");
                    sb.AppendLine("    cil2cpp::capture_last_pinvoke_error();");
                    sb.AppendLine("    return __ret;");
                }
                else
                {
                    sb.AppendLine($"    return {entryPoint}({argStr});");
                }
            }

            // C.7.2: [Out] parameter copy-back for struct-by-ref parameters
            // For pointer-typed params ([Out] int* / ref struct), the native function writes
            // through the pointer directly — no explicit copy-back needed.
            // For managed arrays with [Out], copy-back is handled in C.7.3 (LPArray marshaling).

            sb.AppendLine("}");
            sb.AppendLine();
        }
    }

    /// <summary>
    /// Convert C++ managed type to native P/Invoke type per ECMA-335 II.15.5.4.
    /// </summary>
    private string GetPInvokeNativeType(string cppType, IRType? paramType,
        IR.PInvokeCharSet charSet = IR.PInvokeCharSet.Ansi,
        IR.MarshalAsType? marshalAs = null)
    {
        // C.7: [MarshalAs] overrides all default marshaling behavior
        if (marshalAs != null)
        {
            return marshalAs.Value switch
            {
                IR.MarshalAsType.LPStr => "const char*",
                IR.MarshalAsType.LPUtf8Str => "const char*",
                IR.MarshalAsType.LPWStr => "const char16_t*",
                IR.MarshalAsType.LPTStr => "const char16_t*", // Windows default
                IR.MarshalAsType.Bool => "int32_t",
                IR.MarshalAsType.I1 => "int8_t",
                IR.MarshalAsType.U1 => "uint8_t",
                IR.MarshalAsType.I2 => "int16_t",
                IR.MarshalAsType.U2 => "uint16_t",
                IR.MarshalAsType.I4 => "int32_t",
                IR.MarshalAsType.U4 => "uint32_t",
                IR.MarshalAsType.I8 => "int64_t",
                IR.MarshalAsType.U8 => "uint64_t",
                IR.MarshalAsType.R4 => "float",
                IR.MarshalAsType.R8 => "double",
                IR.MarshalAsType.SysInt => "intptr_t",
                IR.MarshalAsType.SysUInt => "uintptr_t",
                IR.MarshalAsType.FunctionPtr => "void*",
                // C.7.3: LPArray → pointer to element type. For managed arrays (e.g. MdArray_1_int32_t*),
                // the extern declaration uses a pointer to the element type (e.g. int32_t*)
                IR.MarshalAsType.LPArray => "void*",
                IR.MarshalAsType.LPStruct => cppType.TrimEnd('*') + "*",
                IR.MarshalAsType.IUnknown or IR.MarshalAsType.IDispatch => "void*",
                _ => cppType, // Fallback to default
            };
        }

        // String marshaling depends on CharSet (ECMA-335 II.15.5.2)
        if (IsStringType(cppType))
        {
            // TODO: ECMA-335 II.15.5.2 — CharSet.Auto maps to Unicode on Windows, Ansi on Unix.
            // Current: Unicode on all platforms (correct for Windows-only builds).
            // Full fix requires cross-platform build target detection.
            return charSet == IR.PInvokeCharSet.Unicode ? "const char16_t*"
                : charSet == IR.PInvokeCharSet.Auto ? "const char16_t*"
                : "const char*"; // Ansi = UTF-8
        }

        if (cppType == "void") return "void";

        // Delegate → C function pointer
        if (IsDelegateType(paramType)) return GetDelegateFunctionPointerType(paramType!);

        // ECMA-335: native int/uint are pointer-sized → void* in native code
        if (cppType is "intptr_t" or "uintptr_t") return "void*";

        // ECMA-335: System.Boolean marshals as Win32 BOOL (4-byte int)
        if (cppType == "bool") return "int32_t";

        // Enum types are aliased to their underlying integer type (using X = int32_t;)
        // For P/Invoke extern "C" declarations, use the underlying type directly.
        if (paramType is { IsEnum: true, EnumUnderlyingType: not null })
            return CppNameMapper.GetCppTypeForDecl(paramType.EnumUnderlyingType);

        // Fallback: look up external BCL enum types (emitted as "using X = int32_t;" aliases).
        // These aren't in _module.Types — they're in ExternalEnumTypes dictionary.
        if (_module.ExternalEnumTypes.TryGetValue(cppType, out var underlyingType))
            return underlyingType;

        return cppType;
    }

    /// <summary>
    /// Check if a C++ type is valid for an extern "C" declaration.
    /// Accepts primitives, pointers, void, and struct types defined in the compilation unit.
    /// </summary>
    private bool IsValidExternCType(string cppType)
    {
        if (cppType.EndsWith("*")) return true;
        if (IsCppPrimitiveType(cppType)) return true;
        if (cppType is "void" or "int32_t") return true; // int32_t from bool→BOOL marshaling
        // Struct types defined in the compilation unit are valid extern "C" types
        if (_knownStructTypes == null) BuildKnownStructTypes();
        return _knownStructTypes!.Contains(cppType);
    }

    /// <summary>
    /// Check if a type is a delegate type.
    /// </summary>
    private static bool IsDelegateType(IRType? type) =>
        type is { IsDelegate: true };

    /// <summary>
    /// Get the C function pointer type for a delegate type.
    /// Looks up the delegate's Invoke method to determine the signature.
    /// Returns "RetType(*)(ParamTypes...)" syntax.
    /// </summary>
    private static string GetDelegateFunctionPointerType(IRType? delegateType)
    {
        if (delegateType == null) return "void(*)()";

        var invoke = delegateType.Methods.FirstOrDefault(m => m.Name == "Invoke");
        if (invoke == null) return "void(*)()";

        var retType = invoke.ReturnTypeCpp == "void" ? "void"
            : invoke.ReturnTypeCpp == "cil2cpp::String*" ? "const char*"
            : invoke.ReturnTypeCpp;
        var paramTypes = invoke.Parameters
            .Select(p => p.CppTypeName == "cil2cpp::String*" ? "const char*" : p.CppTypeName)
            .ToList();

        return $"{retType}(*)({string.Join(", ", paramTypes)})";
    }

    private static bool IsStringType(string cppType) =>
        cppType is "cil2cpp::String*";

    /// <summary>
    /// Emit __init_runtime_vtables() — patches runtime built-in TypeInfos with codegen VTable
    /// and interface data. Must be emitted AFTER all VTable/interface data in the data file
    /// so that static variables are visible.
    /// </summary>
    private void EmitRuntimeVTablePatching(StringBuilder sb, bool needsStringAlias, bool needsObjectAlias)
    {
        // Check if String has VTable and interface data generated
        // String is NOT CoreRuntime, so VTable/interface data should exist
        var stringType = _userTypes.FirstOrDefault(t => t.ILFullName == "System.String");
        bool hasStringVTable = stringType != null && stringType.VTable.Count > 0
            && !(stringType.IsRuntimeProvided && IRBuilder.CoreRuntimeTypes.Contains(stringType.ILFullName));
        bool hasStringInterfaces = stringType != null && stringType.Interfaces.Count > 0;

        if (!hasStringVTable && !needsStringAlias) return;

        sb.AppendLine("// ===== Runtime TypeInfo VTable Patching =====");
        sb.AppendLine("void __init_runtime_vtables() {");

        // Register Task TypeInfo so runtime-created tasks (task_delay, etc.) have proper vtable
        var taskType = _userTypes.FirstOrDefault(t => t.ILFullName == "System.Threading.Tasks.Task");
        if (taskType != null)
        {
            sb.AppendLine("    // Register Task TypeInfo for runtime task creation");
            sb.AppendLine($"    cil2cpp::task_set_typeinfo(&{taskType.CppName}_TypeInfo);");
        }

        // Patch System.Object's runtime TypeInfo with generated VTable.
        // The runtime defines System_Object_TypeInfo with vtable=nullptr.
        // Without this patch, virtual calls (GetHashCode, Equals, ToString) on
        // plain Object instances crash with null dereference.
        var objectType = _userTypes.FirstOrDefault(t => t.ILFullName == "System.Object");
        if (objectType != null && objectType.VTable.Count > 0)
        {
            sb.AppendLine("    // Patch System.Object TypeInfo with generated VTable");
            sb.AppendLine($"    System_Object_TypeInfo.vtable = &{objectType.CppName}_VTable;");
        }

        // Register reflection TypeInfos so runtime-created ManagedMethodInfo/FieldInfo objects have proper vtables
        {
            var methodInfoType = _userTypes.FirstOrDefault(t => t.ILFullName == "System.Reflection.MethodInfo");
            var fieldInfoType = _userTypes.FirstOrDefault(t => t.ILFullName == "System.Reflection.FieldInfo");
            var paramInfoType = _userTypes.FirstOrDefault(t => t.ILFullName == "System.Reflection.ParameterInfo");
            var propInfoType = _userTypes.FirstOrDefault(t => t.ILFullName == "System.Reflection.PropertyInfo");
            if (methodInfoType != null || fieldInfoType != null || paramInfoType != null || propInfoType != null)
            {
                sb.AppendLine("    // Register reflection TypeInfos for runtime object creation");
                sb.Append("    cil2cpp::reflection_set_typeinfos(");
                sb.Append(methodInfoType != null ? $"&{methodInfoType.CppName}_TypeInfo" : "nullptr");
                sb.Append(", ");
                sb.Append(fieldInfoType != null ? $"&{fieldInfoType.CppName}_TypeInfo" : "nullptr");
                sb.Append(", ");
                sb.Append(paramInfoType != null ? $"&{paramInfoType.CppName}_TypeInfo" : "nullptr");
                sb.Append(", ");
                sb.Append(propInfoType != null ? $"&{propInfoType.CppName}_TypeInfo" : "nullptr");
                sb.AppendLine(");");
            }

            // Patch PropertyInfo/RuntimePropertyInfo vtable slots for get_PropertyType and GetIndexParameters.
            // These are abstract on PropertyInfo, overridden by RuntimePropertyInfo which is ReflectionAliased
            // (all instance methods blocked at codegen), leaving null vtable slots.
            var runtimePropInfoType = _userTypes.FirstOrDefault(t => t.ILFullName == "System.Reflection.RuntimePropertyInfo");
            if (propInfoType != null || runtimePropInfoType != null)
            {
                sb.Append("    cil2cpp::reflection_patch_property_vtables(");
                sb.Append(propInfoType != null ? $"&{propInfoType.CppName}_TypeInfo" : "nullptr");
                sb.Append(", ");
                sb.Append(runtimePropInfoType != null ? $"&{runtimePropInfoType.CppName}_TypeInfo" : "nullptr");
                sb.AppendLine(");");
            }
        }

        if (hasStringVTable)
        {
            // Patch BCL TypeInfo (cil2cpp::System::String_TypeInfo) — used by string_literal/string_create_utf8
            sb.AppendLine("    // Patch BCL String TypeInfo (used by runtime string functions)");
            sb.AppendLine("    cil2cpp::System::String_TypeInfo.vtable = &System_String_VTable;");
            sb.AppendLine("    cil2cpp::System::String_TypeInfo.base_type = &System_Object_TypeInfo;");
            if (hasStringInterfaces)
            {
                sb.AppendLine("    cil2cpp::System::String_TypeInfo.interfaces = System_String_interfaces;");
                sb.AppendLine($"    cil2cpp::System::String_TypeInfo.interface_count = {stringType!.Interfaces.Count};");
                sb.AppendLine("    cil2cpp::System::String_TypeInfo.interface_vtables = System_String_interface_vtables;");
                sb.AppendLine($"    cil2cpp::System::String_TypeInfo.interface_vtable_count = {stringType.InterfaceImpls.Count};");
            }

            if (needsStringAlias)
            {
                // Also patch reflection TypeInfo (cil2cpp::System_String_TypeInfo) — used by typeof/GetType
                sb.AppendLine("    // Patch reflection String TypeInfo (used by typeof/GetType)");
                sb.AppendLine("    cil2cpp::System_String_TypeInfo.vtable = &System_String_VTable;");
                sb.AppendLine("    cil2cpp::System_String_TypeInfo.base_type = &System_Object_TypeInfo;");
                if (hasStringInterfaces)
                {
                    sb.AppendLine("    cil2cpp::System_String_TypeInfo.interfaces = System_String_interfaces;");
                    sb.AppendLine($"    cil2cpp::System_String_TypeInfo.interface_count = {stringType!.Interfaces.Count};");
                    sb.AppendLine("    cil2cpp::System_String_TypeInfo.interface_vtables = System_String_interface_vtables;");
                    sb.AppendLine($"    cil2cpp::System_String_TypeInfo.interface_vtable_count = {stringType.InterfaceImpls.Count};");
                }
            }
        }

        // Initialize String.Empty — a runtime-intrinsic field that .NET pre-initializes
        // before any managed code runs. Since System.String is a RuntimeProvidedType,
        // its .cctor is not compiled from IL, so we must set it here.
        if (stringType != null)
        {
            sb.AppendLine("    // Initialize String.Empty (runtime-intrinsic)");
            sb.AppendLine("    System_String_statics.f_Empty = cil2cpp::string_literal(\"\");");
        }

        // Patch primitive TypeInfos with vtable/interface data.
        // PrimitiveTypeInfos are emitted as minimal stubs (no vtable/interfaces).
        // If the primitive type also exists as a full IRType, patch the TypeInfo
        // with the VTable, base_type, and interface data so that reflection
        // operations (IsAssignableFrom, CanCastTo, etc.) work correctly.
        foreach (var entry in _module.PrimitiveTypeInfos.Values)
        {
            var runtimeAlias = RuntimeTypeRegistry.GetRuntimeTypeInfoName(entry.ILFullName);
            if (runtimeAlias != null) continue; // runtime-provided types are handled separately

            var irType = _userTypes.FirstOrDefault(t => t.ILFullName == entry.ILFullName);
            if (irType == null) continue;

            bool hasVTable = !irType.IsInterface && !irType.IsDelegate && irType.VTable.Count > 0;
            bool hasInterfaces = irType.Interfaces.Count > 0;
            bool hasInterfaceVtables = irType.InterfaceImpls.Count > 0;
            var hasBaseType = irType.BaseType != null;

            if (!hasVTable && !hasInterfaces && !hasBaseType) continue;

            sb.AppendLine($"    // Patch {entry.ILFullName} primitive TypeInfo");
            if (hasBaseType)
                sb.AppendLine($"    {entry.CppMangledName}_TypeInfo.base_type = &{irType.BaseType!.CppName}_TypeInfo;");
            if (hasVTable)
                sb.AppendLine($"    {entry.CppMangledName}_TypeInfo.vtable = &{irType.CppName}_VTable;");
            if (hasInterfaces)
            {
                sb.AppendLine($"    {entry.CppMangledName}_TypeInfo.interfaces = {irType.CppName}_interfaces;");
                sb.AppendLine($"    {entry.CppMangledName}_TypeInfo.interface_count = {irType.Interfaces.Count};");
            }
            if (hasInterfaceVtables)
            {
                sb.AppendLine($"    {entry.CppMangledName}_TypeInfo.interface_vtables = {irType.CppName}_interface_vtables;");
                sb.AppendLine($"    {entry.CppMangledName}_TypeInfo.interface_vtable_count = {irType.InterfaceImpls.Count};");
            }
        }

        // Register all TypeInfos with the runtime type registry.
        // This enables MakeGenericType, type_get_by_name, and other
        // runtime type lookup operations used by BCL reflection code.
        sb.AppendLine("    // Register all TypeInfos for runtime type lookup");
        foreach (var type in _userTypes)
        {
            if (!string.IsNullOrEmpty(type.ILFullName))
                sb.AppendLine($"    cil2cpp::type_register(&{type.CppName}_TypeInfo);");
        }

        sb.AppendLine("}");
        sb.AppendLine();
    }
